  <h2>1906 Tsunami</h2>
          <p>
  A small, 4 inch high, <a
  href="http://walrus.wr.usgs.gov/tsunami/1906.html">tsunami
  was generated by the 1906</a> earthquake and was
  measured at the Presidio tidal gauge.  The tsunami
  rapidly decreased in size as it entered San Francisco
  Bay. Modeling of this tsunami provided useful
  information regarding the location of the epicenter
  offshore and the nature of the initial rupture
  process.
          </p>
