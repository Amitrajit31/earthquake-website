<p>
  No one can predict earthquakes, but after a large earthquake, it’s a pretty safe bet that there will be aftershocks. Aftershocks are “triggered” earthquakes that occur during the days, months, and even years after the mainshock.  Aftershock sequences tend to start strong and die off slowly, although in some cases activity can pick up again if a large enough aftershock occurs. USGS scientists Nicholas van der Elst and Morgan Page wanted to see if a very simple method could give an accurate answer to the question – “Given the number of aftershocks we’ve observed so far, how many more are we likely to see in the future?”
</p>

<br style="clear:left;"/>
<div class='sidebar'>
  A <strong>model</strong>, in this sense, is a set of mathematical equations that represents what we know about a physical system and how it generally works.
</div>

<p>
  There are a variety of different methods for forecasting the behavior of an aftershock sequence, but most of them rely on inputting data into a statistical model of aftershock behavior and then projecting that model into the future. The result depends on the model used and the input data, and although these models can capture the average behavior seen in aftershock sequences, they don’t necessarily take into account the full range of possible outcomes.  Sometimes we see more aftershocks than the models predict; sometimes less.
</p>
