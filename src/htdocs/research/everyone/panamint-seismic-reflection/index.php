    <p>
      Panamint Valley in southern California stretches out across the horizon like a photo from the surface of Mars.  USGS scientists, along with colleagues from Oregon State University and University of Kentucky, will be surveying along the quiet roads in this remote area on the southwestern edge of Death Valley National Park on April 23 to May 4 in an effort to figure out what type of fault is active there.
    </p>
    <p>
      The eastern edge of Panamint Valley has two types of faults that can be seen in the near-surface geology: low-angle faults at the mountain front and high-angle faults in the valley.   To determine which type of fault is active there, the scientists must understand how these faults move deep below the surface. The answer will ultimately reveal whether faults in the region can break in complex combinations of low- and high-angle faults, or if one or the other of these is most likely to rupture alone.
    </p>
