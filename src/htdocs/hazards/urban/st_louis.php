<h2>Is there earthquake hazard in the St. Louis region?</h2>
<p>
  Yes. The St. Louis area has experienced <a href="http://www.eas.slu.edu/eqc/eqc_publ/Flyers/PDFs/St.Louisfinal3.pdf">minor earthquake damage at least 12 times in the past 205 years</a> (PDF, St. Louis University). The St. Louis metropolitan area, with a population of about 2.8 million, faces earthquake hazard from distant large earthquakes in the New Madrid and Wabash Valley seismic zones, as well as a closer region of diffuse historical and prehistoric seismicity to its south and east. Also, low attenuation of seismic energy in the region (seismic energy carries further with less weakening of the signal than in the western U.S.) and a substantial number of historic older unreinforced brick and stone buildings make the St. Louis area vulnerable to moderate earthquakes at relatively large distances compared to the western United States.
</p>

<h2>Local Geologic Variations Affect the Earthquake Hazard</h2>
<figure class='right'>
  <a href="stlouis-haz.php">
    <img src= "images/stlouis-haz.gif" alt='thumbnail of St. Louis area hazards'>
  </a>
  <figcaption>
    Click on image to view full size with caption. (PDF)
  </figcaption>
</figure>

<p>
  Structures located in the Missouri and Mississippi River floodplains will likely experience stronger ground shaking and a greater likelihood of <a href="/learn/glossary/?term=liquefaction">liquefaction</a>. Below St. Louis, bedrock is most commonly a hard 350 million-year-old limestone in the St. Louis area. Structures built on or near <a href="/learn/glossary/?term=bedrock">bedrock</a>, such as in the upland areas out of the floodplains, will tend to have lower levels of earthquake ground shaking. Thus, variation in earthquake-shaking hazard in the area can be subdivided into two main groups defined by surficial geology: uplands and lowland river valleys.
</p>
<p>
  At <strong>short <a href="/learn/glossary/?term=period">periods</a> (high <a href="/learn/glossary/?term=frequency">frequency</a> vibrations)</strong>, seismic hazard is higher on the bluffs (uplands), mostly in the central and eastern parts of the study area, and exceeds 0.40g (ground shaking forces expressed as a percentage of the strength of the Earth’s gravitational force, g), while in the lowlands high-frequency shaking hazard is reduced (less than 0:40g, but still potentially damaging). At <strong>long periods (longer wavelength shaking; 1.0 second period waves)</strong>, the uplands/lowlands trend is reversed with higher seismic hazard (greater than 0.25g) in the lowlands (see figure at right). This is especially true along the Mississippi River due to energy trapped in the thicker soft sediments, whereas being less than 0.25g in the uplands (thin soils).
</p>
<p>
  Liquefaction hazard is characterized as very low (liquefaction unlikely) to severe (liquefaction very likely), with the severe liquefaction hazard occurring in the Mississippi, Missouri, and Meramec River floodplains where there are shallow water tables and 10–40-m-thick sequences of unconsolidated sands and gravels over sedimentary rocks.
</p>

