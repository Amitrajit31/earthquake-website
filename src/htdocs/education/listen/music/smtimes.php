<h3>By Matthew Fordahl</h3>
<p>
  This article, originally by Matthew Fordahl of the Associated Press, appeared
  in the San Mateo County Times with slight modifications.
</p>

<p>
  <img src="images/davidschaff.jpg" width="150" height="119" alt="photo of David Schaff" />
</p>
<p>
  HALF MOON BAY -- The hills are alive with the sound of music -- and so are
  the Earth's valleys, mid-ocean ridges and volcanoes.
</p>
<p>
  Earthquake waves that topple buildings, raise mountains and split the ground
  have long been used to study tremors and the structure of the Earth.
</p>
<p>
  Now, Half Moon Bay seismologist and trombone player Andrew Michael is turning
  them into sound to make truly hard-rock music that can help teach seismology.
</p>
<p>
  Though seismic waves make no noise on their own, their motions as recorded
  by seismometers can be sped up to produce sounds, says Michael, who works at
  the U.S. Geological Survey in Menlo Park and has composed a quake quartet.
</p>
<p>
  "The music came from a concept of how the Earth strains to create earthquakes
  and earthquakes providing a backdrop to civilization," he said. "The general
  population is almost completely unaware until there's a very large earthquake."
</p>
<p>
  Michael's piece for trombone, cello, vocalist and earthquake is about the
  strain that leads to quakes as well as their impact on society. It premiered
  in December at the annual meeting of the American Geophysical Union.
</p>
<p>
  The musicians plan to perform the quartet again on May 13 at the USGS during
  its open house.
</p>
<p>
  The two-minute quartet begins with a trombone and cello glissando punctuated
  by the thundering roars generated from the 1992 Landers earthquakes. The sliding
  noise represents the buildup in the Earth before a quake strikes.
</p>
<p>
  A jazzy movement, which quotes from a Bach cello sonata, is interrupted by
  the quakes' thunder-like rumbling. The glissando then returns and the piece
  ends abruptly with the thud of a big quake.
</p>
<p>
  "It made a good percussion track," said Stephanie Ross, Michael's wife who
  sings the quartet's wordless vocal line. She's also a researcher at the USGS
  and sings with the Coastside Chorale.
</p>
<p>
  Michael said he chose instruments that mirror the waves and motion of quakes.
</p>
<p>
  "The cello is a natural analogy," he said. "The way a cello bow sticks and
  slips against the strings is similar to some of the ideas we have about how
  faults stick and slip."
</p>
<p>
  Michael's interest in music and earthquakes began in 1997, when he was preparing
  a lecture on earthquake waves trapped in fault zones. It struck him that the
  situation was similar to sound waves trapped and vibrating inside a trumpet.
</p>
<p>
  The parallels didn't work for that lecture because of the fleeting nature
  of earthquake waves in a small area. "But I started thinking more about the
  comparisons and decided to do a whole lecture on the analogy," he said.
</p>
<p>
  Seismic waves are much more complex than sound waves, but both transmit energy
  from a source through a path like the ripples from a pebble dropped in a pond
  or a spring compressing or decompressing.
</p>
<p>
  Earthquake waves move too slowly to make audible sound, though many people
  hear the rumbling of objects being shaken. To actually hear a quake, Michael
  speeds up recordings of their waves.
</p>
<p>
  Seismograms -- the squiggly line graphs that show ground motion -- measure
  several types of waves. To become sound, the entire range of a quake's motion
  is reduced to the in-out motion of a speaker, which vibrates to make sound.
</p>
<p>
  "We are playing fast and loose by taking the whole seismogram and turning
  it all into compressions and noncompressions," he said. "But it works well enough."
</p>
<p>
  And it has scientific value.
</p>
<p>
  "It puts us in the position of being the music critic to the Earth," said
  Michael, a trombonist since childhood who plays in the Coastside Community Orchestra.
</p>
<p>
  By listening, scientists can tell how much a fault slipped. Smaller faults,
  like small musical instruments such as trumpets, produce higher frequency waves
  than longer faults, or big instruments like tubas.
</p>
<p>
  So a magnitude-6.3 quake from a small fault has a higher pitch than a magnitude-6.5
  quake generated along a larger fault.
</p>
<p>
  Quakes recorded from a distance have a much deeper sound resembling thunder.
  Closer quakes, on the other hand, sound more like a gunshot.
</p>
<p>
  The reason? High frequency waves dissipate more quickly over distance, leaving
  only a low rumble. It's not unlike the boom heard through the wall from a neighbor's
  stereo. The higher-pitch lyrics and melodies don't travel as far.
</p>
<p>
  Michael so far has presented his lecture fewer than a dozen times at universities
  and scientific conferences. He hopes someday to make it available on video.
</p>
<p>
  "There's actually been a good reaction from the general public," he said.
  "It's spoken to different people in different ways. People are actually learning
  some seismology and music."
</p>
<p>
  Fittingly, one of Michael's musical influences is the American composer Charles
  Ives, who experimented with polyrhythm and atonality in the early 1900s.
</p>
<p>
  "He gave musicians the permission to try almost everything," Michael said.
</p>
