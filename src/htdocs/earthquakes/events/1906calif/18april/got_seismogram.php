<p> P (the direct, compressional wave) travels fastest and marks the first arrival of waves from the earthquake. S marks the arrival of the slower S or shear wave. P waves generally do not cause as much shaking as some of the larger, later arriving shear waves. The lines PP, PPP, SS and SSS indicate the arrivals of waves which have bounced once or twice off the surface of the earth in travelling to the seismograph.</p>

<p>A <a href="earthwaves.php">brief explanation</a> of these "body" waves that travel inside the earth.</p>

<p> The actual maximum amplitude of the shaking at the Gottingen observatory during the time of the record shown was less than 1 mm or about 0.04 inches, until the surface waves arrived when the instrument went off scale. (Surface waves, not surprisingly, travel along the earth's surface, rather than through it as do P and S waves.) </p>

