<h2>Introduction</h2>

<figure class='right'>
  <img src='images/locationmap.jpg' alt=''/>
  <figcaption>
    The Teton Fault is shown here as a red line following the base of the Teton Mountain Range. The green outline shows the boundary of Teton National Park.
  </figcaption>
