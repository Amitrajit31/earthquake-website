<p> The 1906 earthquake was the first earthquake in the U.S. to be scientifically
  studied, and the State Earthquake Investigation Commission (SEIC) published
  a very thorough and important scientific report, published in 1908 (A.C.
  Lawson, ed.).</p>

<p> Recent research in historical archives has provided important new insights
  into the 1906 rupture along the San Andreas, particularly along the San
  Francisco peninsula and in the <a href="historical_lp.php">
  Loma Prieta area</a>.</p>

<p> A collection of about 600 glass plate negatives, many unpublished, is
  in the archives of the Bancroft Library at the University of California,
  Berkeley. Photographs such as those below, that show the 1906 ground rupture,
  are being used to map the fault more precisely than has been previously
  possible through areas that are now developed.</p>

<p> To get an impression of the extent of development in one area compare
  the scanned <a href="images/carol_map2.html">1950</a> (817K) and <a href="images/carol_map.html">1980</a>
  (850K) topo maps, of the San Francisco S quad.</p>

<p> Development in many cases has obliterated the traces of the 1906 rupture,
  but using old maps and pre-development aerial photographs researchers
  have been able to locate many of the historic photographs. The photographs
  are also useful for determining the amount of 1906 offset and the width
  of the fault zone.</p>


