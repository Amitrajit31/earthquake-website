<p>
	On January 4, 1998, the Albuquerque Seismological Laboratory seismic station ANMO recorded an earthquake with a duration magnitude of 3.8. This earthquake occurred southeast of Albuquerque, just south of Willard, New Mexico. This earthquake is only a few miles from the seismic activity which occurred on New Years Eve. The earthquake plotted (in black) above is the largest of the recent earthquakes which occurred in the area. A microearthquake aftershock with a duration magnitude of less than 1.0 occurred in the same vicinity, approximately 200 seconds after the arrival of the P-wave of the main event.
</p>

<p>
	Plotted in blue and offset above the large seismogram, is the largest of the 5 earthquakes of December 31, 1997, recorded at ANMO, of which at least one was locally felt. This earthquake occurred southeast of Albuquerque, approximately 6 miles northeast of Mountainair, New Mexico. Both earthquakes have been plotted at the same scale. Notice how much larger the amplitude and longer the duration of 3.8 earthquake relative to the 3.0 earthquake.
</p>

<p>
	The arrival times of P and S waves are often used to calculate the origin time and location of earthquakes.&nbsp; A <em>S-P</em> time of 6.4 seconds gives a distance of approximately 32 miles from the seismic station for the Jan 4, 1998 event, when inserted into an equation which solves for distance taking local geology into account. An analysis of the ground motion recorded on the horizontal components at ANMO (not shown) give a line of south-southeast/north-northwest, coupled with reports or signals recorded from other seismic stations, the direction to the earthquake can be calculated. Other signals recorded by the
	<a href="http://www.ees.nmt.edu/Geop/NMTSO.html">New Mexico Tech Seismic Network</a> were used to "triangulate" a more precise location of the earthquakes. The magnitude given is a duration magnitude, calculated by plugging in the time taken for the recorded signal to return to background noise levels into an equation which accounts for local geology. The duration magnitude is similar to Richter magnitude in that it is a logarithmic scale; however, Richter magnitude is based on the amplitude, rather than the duration, of the recorded signal.
</p>

<p>
	You may also wish to view the past 24 hours of seismic recordings from the
	<a href="/monitoring/operations/stations/IU/ANMO/">ANMO station</a>. This plot is updated every ten minutes, and shows primarily the high frequency seismic energy which is visible from nearby earthquakes. Most of the high frequency energy from large, distant, earthquakes is attenuated while the energy travels from the earthquake to the recording station. Thus, the signal from some large earthquakes may appear smaller on this plot than the signal from much smaller, nearby earthquakes.
</p>
