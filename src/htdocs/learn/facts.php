    The greatest mountain range in the world, though, is the <strong<Mid-Ocean
    Ridge</strong>, extending 64,374 km (40,000 mi) from the Arctic Ocean to the Atlantic Ocean, around Africa, Asia, and Australia, and under the Pacific Ocean to the west coast of North America. It has a greatest height of 4207m (13,800 ft) above the base ocean depth.
  </li>
  <li>
    As far as we know, there is no such thing as <b>"earthquake weather"</b>.
    Statistically, there is an equal distribution of earthquakes in cold weather,
    hot weather, rainy weather, etc.   If weather does affect earthquake occurrence,
    we do not yet understand how it works.
  </li>
  <li>
    From 1975-1995 there were only <strong>four states that did not have any
    earthquakes</strong>. They were: Florida, Iowa, North Dakota, and
    Wisconsin.
  </li>
  <li>
    The <strong>core of the earth</strong> was the first internal structural
    element to be identified. In 1906 R.D. Oldham discovered it from his
    studies of earthquake records. The inner core is solid, and the outer core
    is liquid and so does not transmit the shear wave energy released during
    an earthquake.
  </li>
  <li>
    The swimming pool at the University of Arizona in Tucson lost water from
