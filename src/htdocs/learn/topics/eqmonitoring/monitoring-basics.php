<h2>P Waves</h2>
<p>
	<img src="images/p_image.gif" alt="Cartoon image showingl "P" wave travel in the earth."/>
</p>
<p>
	The fastest wave, and therefore the first to arrive at a given location.<br />
  Also known as compressional waves, the P wave alternately compresses and expands material in the same direction it is traveling.<br />
  Can travel through all layers of the Earth.<br />
  Generally felt by humans as a bang or thump. <br />
</p>

<h2>S Waves</h2>
<p>
	<img src="images/s_image.gif" alt=""/>
</p>
<p>
	The S wave is slower than the P wave and arrives next, shaking the ground up and down and back and forth perpendicular to the direction it is traveling.<br />
  Also know as shear waves. <br />
</p>

<h2>Surface Waves</h2>
<p>
	<img src="images/surface_image.gif" alt="Cartoon image showing the travel of the "surface" wave in the earth"/>
</p>
<p>
	Surface waves follow the P and S waves.<br />
  Also known as Rayleigh and Love waves.<br />
  These waves travel along the surface of the earth.
</p>

