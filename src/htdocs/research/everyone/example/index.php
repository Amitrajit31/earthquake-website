<h2>Washington DC Stone and Brick Buildings Vulnerable to Distant Quakes</h2>

<figure class='right wa-mon'>
  <img src="images/wa-monument.jpg" alt=""/>
  <figcaption>
    Photo of the pyramid top of the Washington Monument from the U.S Park Police Helicopter. (photo courtesy of the National Park Service)
  </figcaption>
</figure>

<p><i>
  New study shows that DC geologic conditions strongly affect earthquake shaking.
</i></p>

<p>
  On August 23, 2011 a <a href="https://earthquake.usgs.gov/earthquakes/events/2011virginia/">magnitude 5.8 earthquake in Mineral, Virginia</a> rattled Washington, DC, 130 km (81 mi) away, and caused some tens of millions of dollars in widely-publicized damage.  Prominent among the damaged buildings and structures were concentrated damages to the pyramid top of the Washington Monument; the Sherman Building at the Armed Forces Retirement Home; parts of the Washington National Cathedral such as the flying buttresses and spires; and the chimneys and other small parts of the Smithsonian “Castle”.  Minor damage around the city included cracked masonry and chimneys, other broken architectural elements, and toppled gravestones.
</p>

<h3>How could a moderate-sized earthquake so far away cause so much damage?</h3>

<figure class='left dyfi'>
  <img src="images/felt-comparisons.gif" alt=""/>
  <figcaption>
    An earthquake of the same size in the Eastern U.S. is felt more widely than one in the Western U.S. because seismic waves travel further in the colder, denser crust of the Eastern U.S.
  </figcaption>
</figure>

<p>
  Older, denser crustal rocks allow earthquake wave energy to travel greater distances in the central and eastern parts of the United States than through the younger, less consolidated rocks in the western part of the country.  It’s also well known that older stone and brick buildings and other unreinforced structures, which are more prevalent in the east than in the west, are more susceptible to shaking damage from an earthquake.  But even so, the observed damages in some of the structures, and even the <a href="/earthquakes/eventpage/se609212#dyfi">citizen-science reports in the “Did You Feel It?” system</a>, indicated stronger shaking in Washington, DC, than would be expected from the Virginia earthquake.
</p>

<h3>What caused the increased ground motion in the DC area?</h3>
<p>
  That’s exactly what Thomas Pratt and his colleagues aimed to find out when they installed 27 temporary seismic instruments around the DC area in late 2014. Even though there are few earthquakes in that area, the sensitive instruments recorded 30 earthquakes in the U.S. and around the world during the 10 months they were in place.
</p>

<p>
  When they examined the recordings of the earthquakes, they discovered that shaking in some parts of the DC area was larger than expected in the areas underlain by sediments.
</p>

<figure class='left'>
  <img src="images/waveforms.gif" alt=""/>
  <figcaption>
    Recordings of a magnitude 3.0 earthquake beneath western North Carolina at two bedrock sites (DC01 and 50 DC02) and two sites on moderately thick sediments (DC05 and DC29). Note the larger amplitudes and longer durations of the recordings at sites on the sediments.  (from Pratt et al., 2017)
  </figcaption>
</figure>

<h3>Can shaking amplification happen in other areas?</h3>
<p>
  There are many other central and eastern North American cities sitting entirely or partially on sedimentary layers underlain by older, stronger bedrock similar to the geologic conditions in Washington, DC.  The scientists who performed this research presume that ground shaking amplification can occur in these other locations too.
</p>
<p>
  Shaking amplification due to soft sediments, combined with the long distances seismic waves travel in eastern North America and the large numbers of older, more fragile buildings could result in increased earthquake risk for much of the eastern U.S. and Gulf Coast regions.
</p>

