  <li>
    The <strong>cause of earthquakes was stated correctly in 1760</strong> by
    British engineer John Michell, one of the first fathers of seismology, in
    a memoir where he wrote that earthquakes and the waves of energy that they
    make are caused by &ldquo;shifting masses of rock miles below the
    surface&rdquo;.
  </li>

