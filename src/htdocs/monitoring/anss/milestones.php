<h3>2017</h3>
<ul>
  <li>
    The USGS and its university ANSS partners roll out version 1.2 of the ShakeAlert earthquake early warning system .  This milestone extends the “production prototype” that was rolled out in California last year to Washington and Oregon creating a fully integrated ShakeAlert system for the West Coast.
  </li>
  <li>
    The USGS Earthquake Hazards Program assumes support of the Central and Eastern U.S. Seismic Network, also known as the N4 network.
  </li>
  <li>
    A new 2017 collection of nearly 800 ShakeMap earthquake scenarios is released for the continental U.S.
  </li>
</ul>

<h3>2016</h3>
<ul>
  <li>A White House Earthquake Resilience Summit was held, which included sessions on earthquake early warning.  The USGS and its university partners announced the deployment of the next-generation ShakeAlert&trade; production prototype.  The Gordon and Betty Moore foundation announced $3.6 million in grants to university partners to advance the ShakeAlert&trade; system.</li>
  <li>The USGS National Strong Motion Program's recent improvements to the Anchorage Strong Motion Network result in excellent data being recorded in the M7.1 "Iniskin" earthquake in Alaska.</li>
  <li>USGS Earthquake Hazards Program and NEIC respond to the M5.8 Pawnee earthquake in Oklahoma.</li>
</ul>

<h3>2015</h3>
<ul>
  <li>USGS Earthquake Hazards Program and NEIC respond to M7.8 "Gorkha" Nepal earthquake and its aftershock sequence.</li>
  <li>In 2015, the USGS had about 60 temporary seismic stations deployed to monitor possible induced seismicity sequences and coordinated with existing or developing State seismic networks in Oklahoma, Kansas, and Texas.</li>
  <li>USGS supports work to upgrade and construct approximately 150 seismic stations as part of the West Coast earthquake early warning system.</li>
</ul>

<h3>2014</h3>
<ul>
  <li>ShakeCast released version 3.0, which allows the flexibility to be run in the cloud, resulting in lower costs for users by eliminating hardware and software requirements.</li>
	<li>The USGS and ANSS partner at UC Berkeley respond to the 6.0 South Napa earthquake.</li>
	<li>The demonstration ShakeAlert&trade; Earthquake Early Warning system, built upon ANSS by USGS and its university partners, performed well during the M6.0 South Napa earthquake.</li>
	<li>The FEMA funded Urban Areas Security Initiative (UASI) supported station upgrades or new seismic station installations at 125 locations in the Southern California Seismic Network to contribute toward station density and robust communications to advance earthquake early warning by the USGS and ANSS partner Caltech.</li>
  <li>USGS and its university partners publish a technical implementation plan for the ShakeAlert&trade; West Coast earthquake early warning system, which spells out the steps needed to complete the system.</li>
</ul>

<h3>2013</h3>
<ul>
	<li>National Strong Motion Project completes installation of advanced structural health monitoring systems in 24 Veterans Affairs hospitals, as part of an effort to instrument such hospitals in seismically active areas. Instrumentation of an additional 4 hospitals remains underway</li>
	<li>The USGS rolls out new mobile friendly Latest Earthquake map and list.</li>
</ul>

<h3>2012</h3>
<ul>
	<li>Earthquake alerts offered via twitter accounts (@USGSted and @USGSBigQuakes)</li>
	<li>Subscriptions to the USGS Earthquake Notification Service (ENS) exceed 350,000</li>
	<li>Almost 400 NetQuakes instruments installed throughout the ANSS</li>
	<li>ANSS partners--the University of Washington Pacific Northwest Seismic Network, USGS Hawaiian Volcano Observatory, USGS Alaska Volcano Observatory, University of Utah Seismograph Stations, Lamont Doherty Earth Observatory and the University of Memphis Center for Earthquake Research and Information--begin operating a new processing system called ANSS Quake Monitoring System (AQMS)</li>
	<li>Completed installation of structural monitoring system (72 channels) in the Rincon One South condo tower in San Francisco, at joint project with the California Geological Survey</li>
</ul>

<h3>2011</h3>
<ul>
	<li>Completed upgrades of 500+ ANSS stations under ARRA</li>
	<li>ANSS expands its portable seismograph system to 32 6-channel instruments, including include broadband seismometers and accelerometers</li>
	<li>USGS implements new software system and layout for the recent earthquake pages (earthquake.usgs.gov/earthquakes/map)</li>
	<li>Gordon and Betty Moore Foundation awards $6 million to create a prototype earthquake early warning system on the west coast, in a collaboration between the USGS and seismologists at UC Berkeley, Caltech, and U. Washington</li>
	<li>USGS and ANSS partners respond to aftershock monitoring of M5.3 Trinidad, CO, M5.6 Prague, OK and M5.8 Mineral, Virginia earthquakes</li>
	<li>USGS Earthquake Hazards Program and NEIC respond to M9.0 Tohoku, Japan earthquake, tsunami, and aftershock sequence</li>
</ul>

<h3>2010</h3>
<ul>
	<li>NEIC implements regional moment tensor inversion, providing more rapid estimates of reliable magnitudes (Mw) for M4 and larger events within the continental United States and Canada</li>
	<li>USGS Earthquake Hazards Program and NEIC respond to M8.8 Maule, Chile and M7.0 Port-au-Prince, Haiti earthquakes, including aftershock monitoring in Haiti</li>
	<li>USGS and ANSS partner Caltech respond to M7.2 Baja, Mexico earthquake</li>
</ul>

<h3>2009</h3>
<ul>
	<li>W-phase source inversions implemented at the NEIC, allowing more rapid estimation of precise magnitudes and mechanisms of large, global earthquakes</li>
	<li>Initiation of NetQuakes program to install cost-effective strong motion instruments in urban areas</li>
	<li>USGS allocates $30 million of funding towards improving seismic and geodetic monitoring, as part of the American Recovery and Reinvestment Act (ARRA), of which $19 million is for the modernization of the ANSS</li>
</ul>

<h3>2008</h3>
<ul>
	<li>USGS and ANSS partners the University of Nevada-Reno and University of Utah Seismograph Stations respond to M6.0 Wells, NV earthquake</li>
</ul>

<h3>2007</h3>
<ul>
	<li>Finite fault modeling implemented at the NEIC, providing the capability to map the slip distribution of large earthquakes around the globe</li>
	<li>USGS and ANSS partner the University of Utah Seismograph Stations respond to Crandall Canyon mine collapse</li>
</ul>

<h3>2006</h3>
<ul>
  <li>The <a href="/contactus/golden/neic.php">National Earthquake Information Center began 24x7 operations</a> on January 8, 2006</li>
  <li>USGS supported an ANSS Regional Network Operations Workshop in February, 2006, in Salt Lake City, Utah. A summary of the workshop, along with pictures and a list of participants is available at: <a href="http://www.ceri.memphis.edu/people/withers/NetOps/">http://www.ceri.memphis.edu/people/withers/NetOps/</a></li>
  <li>ANSS received an IT Security Certification and Accreditation in March, 2006, from the Department of the Interior.</li>
  <li>USGS expands funding to ANSS regional seismic networks, from $3.0 million to 2003 to $5.75 million in 2006.</li>
  <li>USGS begins funding investigations on the feasibility of earthquake early warning</li>
</ul>

<h3>2005</h3>
<ul>
	<li>ANSS stations increased by 67 in 2005, to 590.</li>
	<li>ANSS Cost-Benefit Study Completed by the National Research Council</li>
	<li>The ANSS System Response Monitoring Committee completed the &quot;<a href="docs/ANSS_Guideline_civil.pdf">Guideline for ANSS Monitoring of Engineered Civil Systems</a>,&quot; thus establishing a solid foundation for the instrumentation of buildings and other structures under ANSS.</li>
	<li>USGS and the California Geological Survey completed a memorandum of understanding on the development of a &quot;National Center for Engineering<br />
	    Strong Motion Data,&quot; which will support ANSS and CGS analysis, distribution and archiving of the seismic records of earthquakes of importance to engineering and design.</li>
	<li>September: USGS agrees to assume in 2007 the operations and maintenance costs of 39 ANSS Backbone stations that will be installed in 2004-2006 under the USArray element of the <a href="http://www.earthscope.org/">Earthscope Initiative of the National Science Foundation</a></li>
	<li>
	  UCLA Factor Building integrated into ANSS System Response Monitoring;
	  ANSS-CENS cooperative project provides dense, real-time monitoring of a
	  17-story, steel-frame structure.
	    <a href="http://factor.gps.caltech.edu">http://factor.gps.caltech.edu/</a>
		</li>
</ul>


<h3>2004</h3>
<ul>
	<li>
		<b>September 27, 2004</b> - implementation of regional seismic monitoring
		network in Jackson Hole region of Wyoming.
	</li>
	<li>NEIC upgrade code was completed in the Fall and testing was begun.</li>
	<li>Six stations added to seismic backbone network.</li>
	<li>
			PAGER (Prompt Assessment of Global Earthquakes Response) development initiated, combines expected ground motion
			with population and intrastructure information to &quot;predict&quot; impact of a significant earthquake.
	</li>
	<li>ANSS system underwent an IT security audit with positive results.</li>
	<li>
			<b>March 27, 2004</b> - First fully-instrumented structure under ANSS, the Atwood Building in Anchorage, AK,
			and ShakeMap implemented in Achorage region for 40th Anniversary of the 1964 earthquake.
	</li>
</ul>

<h3>2003</h3>
<ul>
	<li>ShakeCast development initiated to deliver specific ShakeMap products to critical users.</li>
	<li>CISN Display, web-enabled earthquake notification system development initiated by CISN.</li>
	<li>Earthquake Summary Posters become standard product for significant earthquakes.</li>
	<li>44 additional ground-based strong-motion stations are installed across the country.</li>
	<li>
		EarthScope and USGS partner to expand national seismic backbone network. providing dual
		satellites, redundancy, and fail-over capabilities.
	</li>
</ul>


<h3>2002</h3>
<ul>
	<li><b>April</b> - Technical Implementation Committee completes first report (version 1.0).</li>
</ul>

<h3>2001</h3>
<ul>
	<li><b>November</b> - Workshop held to define priorities and siting criteria for structural instrumentation.</li>
	<li><b>September</b> - First meeting of fully constituted ANSS National Steering Committee.</li>
	<li>
		<b>Summer</b> - Next 100 ANSS strong motion instruments installed in San Francisco (30),
		Seattle (20), Salt Lake City (20), Reno/Las Vegas (10), Anchorage (10) and Memphis (10).
	</li>
	<li>
		<b>August</b> -  Regions complete FY2002 and 5-year implementation plans and submit to
	 	ANSS National Steering Committee.
	</li>
	<li><b>April</b> - ANSS implementation plan completed and submitted to Department of Interior.</li>
</ul>

<h3>2000</h3>
<ul>
	<li>
		<b>November</b> - Congress authorizes full implementation of ANSS over a five year
		period and requires USGS to submit an implementation plan.
	</li>
	<li>
		<b>Summer</b> - First 80 ANSS strong motion instruments installed in
		San Francisco (40), Salt Lake City (20) and Seattle (20).
	</li>
</ul>

<h3>1999</h3>
<ul>
