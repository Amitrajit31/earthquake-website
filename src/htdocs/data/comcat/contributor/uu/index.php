<h2 id="mission">Mission</h2>
<p>
  Regional seismic monitoring in the IMW Region continues to rely on a geographic patchwork of
  seismic networks, involving both stably-funded and unstably-funded operations.  Well-established
  networks cover most of Utah, Nevada, and Montana.  Throughout the IMW Region and especially in
  Idaho, Wyoming, New Mexico, Colorado, and Arizona the challenges are to achieve adequate
  geographic coverage of seismically hazardous areas, especially in gaps between existing
  networks and in areas that have been historically neglected; stable network operations in
  areas where seismic hazard and/or risk warrants continuous seismic monitoring; and
  coordinated data exchange, uniform earthquake reporting and response, and compilation of
  homogenous earthquake catalogs for more systematically defined regions of responsibility.
</p>
