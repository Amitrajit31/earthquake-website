          <p><strong><a name="kids" id="kids"></a>For Young Readers</strong> </p>
          <p><strong>Fossils Tell of Long Ago</strong>, by Aliki<br />
  Grades K&ndash;3. Paperback: ISBN 0064450937, Hardcover: ISBN 0690048297 <br />
  Tells about the formation of fossils and clues they give us in interpreting past environments.<br />
          </p>
          <p><strong>Geology Crafts for Kids: 50 Nifty Projects to Explore the Marvels of Planet Earth</strong>, by Alan&nbsp;Anderson, Gwen Diehn, Terry Krautwurst <br />
  Grades 4&ndash;7. Paperback: ISBN 0806981571, Hardcover: ISBN 0806981563 <br />
  General earth science activities explore subjects such as volcanoes, fossils, rocks & minerals, and erosion. <br />
          </p>
          <p><strong>Everybody Needs a Rock</strong>, by Byrd Baylor, Peter Parnall <br />
  Grades K&ndash;3. Paperback: ISBN 0689710518, Hardcopy: ISBN 0684138999 <br />
  A neat book full of earnest advice about picking out the perfect "pet" rock. </p>
          <p><strong>Magic School Bus inside the Earth</strong>, by Joanna Cole <br />
  Grade
    <!-- #BeginDate format:En2a -->22-Sep-2006 0:08 AM<!-- #EndDate -->
