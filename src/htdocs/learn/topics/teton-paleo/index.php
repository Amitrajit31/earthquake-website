<p>
  In order to photograph the entire trench wall, a bunch of side-by-side photographs are taken and then “stitched together” into a photomosaic. In order to align the final photomosaic and to keep it from “warping” over long distances, control points are surveyed first. Ultimately we will use the photomosaics of the trench to log observations of the trench walls (e.g. different soil/rock units, faults, displacements, sample locations, etc.).
</p>
<p>
  We also set up a few level lines to reference while in the trench and when we view the photomosaics. With a long, steep trench the ground can act like an optical illusion, so true level is different from what "looks" level to your brain. We have been joking about the trench acting like the Mystery Spot in Santa Cruz because when you string up a level line, it looks very, very off, as if it's back tilted into the hill. We even had to bring out an old fashioned bubble level to compare to the total station survey measurements to confirm that it was correct!
</p>
<p>
  Most of the time we have been here is has been very smoky with fairly stagnant air. Today it was breezy and partially cloudy, and the smoke cleared for the first time all week and we could see all the way across the valley. It's a great view!  We have many visitors from locals and tourists that have been hiking and biking the nearby trails. Most people seem pretty interested to learn how we study past earthquakes.
</p>
<p>
  The team could not resist making speculation about the number of earthquakes visible in the trench, and we are excited to begin interpretation. We have a late night  building the 3D photo model of the trench exposure that will be used to log the trench information.
</p>
