earthquake catalogs of the USGS/NEIC. Cavity collapses associated with nuclear
tests in the Nevada Test Site have been removed from the list. Almost all
the events listed were thought to be induced by mining operations. The USGS/NEIC
did not systematically tabulate U.S. mining-induced events until the mid-1980's.
The list is given in the GSE2.0 format.
