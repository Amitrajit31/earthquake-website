<p>
  At the Corner Canyon site, the scientists also found geologic evidence of six ground-rupturing earthquakes on the Salt Lake City segment in the past 4800 years. For this study, the scientists developed a multiple-method approach to evaluate and model the timing of individual earthquakes at the site. Paleoseismologists typically analyze collected data using statistical analysis, which works well in a geological environment where earthquakes occur among a continuous and uninterrupted sedimentary record.  The approach doesnâ€™t work as well when the sedimentary record is sporadic and/or complex. The new multi-method approach developed in this study more accurately assesses earthquake timing in a complex geological environment.
</p>

