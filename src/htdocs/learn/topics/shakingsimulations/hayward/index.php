<p>
  In 2008 the USGS led a <a href="../people.php">collaborative effort</a> with <a href="http://www.urscorp.com">URS Corporation</a>, <a href="http://www.llnl.gov">Lawrence Livermore National Laboratory</a>, <a href="http://pangea.stanford.edu">Stanford University</a>, and the <a href="http://seismo.berkeley.edu/">University of California at Berkeley</a> to create computer simulations of large, anticipated earthquakes on the Hayward and Rodgers Creek faults. The most recent large earthquake on the <a href="/earthquakes/events/1868calif/">Hayward fault</a> was in 1868, 140 years ago. Because the past five large earthquakes</a> on the Hayward fault have been about 140 years apart, the Hayward and Rodgers Creek faults are the most likely faults to produce a large earthquake in the Bay Area. These computer simulations of scenario (or anticipated) earthquakes provide detailed pictures of what shaking we should expect in such earthquakes. These computer models capture the shaking at length scales larger than about 300 ft (100 m), so they do not include the effects of very thin, soft sediments, such as Bay Mud around the perimeter of San Francisco Bay. For areas with these thin, soft sediments the shaking would be stronger than what is shown in the movies.
  </p>

<p>
  The computer simulations include a total of seven earthquake scenarios: three magnitude 6.8 scenarios with different starting locations (epicenters), three magnitude 7.0 scenarios with different starting locations, and one magnitude 7.2 scenario. The next large earthquake on the Hayward fault will likely fall in the range of magnitude 6.8-7.0. The magnitude 6.8 and magnitude 7.0 scenario earthquakes provide detailed examples of the type of shaking we should expect in such an event. The magnitude 7.2 scenario earthquake, while possible, is a much less likely event involving simultaneous rupture of both the Hayward and Rodgers Creek faults.
</p>

