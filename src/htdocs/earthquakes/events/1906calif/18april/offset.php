<p> At depth the slip was probably larger. A recent study by
                   Thatcher, Marshall, and Lisowski found from a re-analysis
                   of geodetic measurements that there was probably 24 feet of
                   slip at depth near Point Reyes and up to 28 feet near Shelter
                   Cove at the north end of the rupture. Over the southern 100
                   miles of the rupture, slip was much less, averaging 8-12 feet.
