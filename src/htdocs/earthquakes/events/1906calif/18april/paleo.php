<p>The 1906 earthquake was accompanied by nearly 300 miles
of surface rupture along the San Andreas Fault between San
Juan Bautista and Shelter Cove. On the surface, the maximum
measured displacement was about 21 feet, near Olema. The fault
moved in a fashion that geologists of the time did not expect:
it moved horizontally, the southwestern side slipping to the
northwest, relative to the southeastern side. The offsets
measured across the fault in other locations were smaller
than the offset near Olema. Near Point Arena it was about
15 feet. South of San Francisco, it was about 9 feet, and
dropped off to the southeast.</p>

<p>Paleoseismology has helped geologists to learn more about the behavior of the fault
in 1906 and in prehistoric earthquakes.  Paleoseismology involves the geologic study of the past behavior of
active faults. Typical methods include excavating trenches across active
faults to study and date the sediments that have been disrupted by prehistoric
earthquakes. Results of these studies include constraints on the timing
of prehistoric earthquakes, the amount of offset, and how fast the fault
is moving, on the average, over geologic time (slip rate). More information
about the geologically recent past behavior of active faults allows better
forecasts of their future behavior.</p>

