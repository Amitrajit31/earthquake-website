    <p>
      The same process goes on in an earthquake. The stress in the Earth's crust pushes on the fault holding it together and causing frictional resistance between the rocks (&quot;fingers&quot;) on each side. Because of the friction, when the rocks are pushed sideways, they do not slip immediately. Eventually enough slip is built up and the rocks slip suddenly, releasing energy in the form of sound waves and shear waves that travel through the rock to cause the shaking that we feel as earthquakes. Just as you snap your fingers with the whole area of your finger tip and thumb, earthquakes happen over an area of a fault, called the rupture surface. However, unlike your fingers, the slip does not occur over the whole fault plane at once. Slip starts at the hypocenter. The epicenter is the point on the Earth's surface above the hypocenter  and then spreads down the fault. It keeps moving down the fault until it runs into something that stops it (exactly how this happens is one of the hot research topics in seismology.) Each point on that surface radiates energy, so you can be 200 miles from the epicenter and still be on top of the earthquake. Your distance from the fault plane is the most important factor in determining how much shaking you feel from a particular size earthquake.
