<p>
  The central U.S. is not the usual location for large earthquakes in the U.S., but if you were living near the Mississippi River between Memphis and St Louis in 1811-1812, you may have begged to differ. Three large earthquakes in the New Madrid seismic zone between December 1811 and February of 1812 shook a huge area from the mid-West to the East Coast. Historical accounts, as well as many scientific studies, have established their approximate locations (see map) and magnitudes (~M7+). Details of past earthquakes are difficult to come by in this area where evidence of shaking has long since been eroded or covered by sediments from the Mississippi River, or modified by farming. Older earthquakes are even harder to pin down, but evidence from trenching and liquefaction studies have documented evidence for three or four large earthquakes since 4400 years ago. The most recent three earthquakes occurred in the past 1100 years, occurring roughly 550 years apart. Does the recent 550-year recurrence pattern extend into the past? And will it extend into the future? Placing constraints on the timing of past earthquakes is essential for estimating the future earthquake hazard in this region. However, clues regarding past earthquakes are few and far between in this setting.
</p>
<p>
  Some interesting features on new high-resolution lidar datasets (<a href="https://opentopography.org/">Open Topography</a> and <a href="http://tngis.org/lidar">Tennessee GIS</a>) in northwestern Tennessee along the eastern margin of the Mississippi River floodplain recently captured the attention of some USGS scientists. The features are geologic cracks and scarps, called sackungen, on bluffs near the Reelfoot fault in the New Madrid seismic zone (One is a sackung, many are sackungen.) The scientists’ research indicates that the sackungen were likely created by strong shaking from earthquakes on the Reelfoot fault and record evidence for four major earthquakes in this region.
</p>

