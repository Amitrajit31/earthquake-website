<p>From an examination of the displacement of the ground surface
which accompanied the 1906 earthquake, Henry Fielding Reid,
Professor of Geology at Johns Hopkins University, concluded
that the earthquake must have involved an "elastic rebound"
of previously stored elastic stress. </p>
<p> If a stretched rubber band is broken or cut, elastic energy
stored in the rubber band during the stretching will suddenly
be released. Similarly, the crust of the earth can gradually
store elastic stress that is released suddenly during an earthquake.</p>
<p> This gradual accumulation and release of stress and strain
is now referred to as the "elastic rebound theory" of earthquakes.
Most earthquakes are the result of the sudden elastic rebound
of previously stored energy.</p>

<p>The following diagram illustrates the process.
Start at the bottom. A straight fence is built across
the San Andreas fault. As the Pacific plate moves
northwest, it gradually distorts the fence. Just
before an earthquake, the fence has an "S" shape.
When the earthquake occurs the distortion is released
and the two parts of the fence are again straight;
but now there is an offset. </p>
<p> This diagram greatly exaggerates the distortion.
Actually, the distortion is spread over many miles
and can only be seen with precise instrumentation
(e.g. <a href="http://quake.usgs.gov/research/deformation/gps/index.html">GPS</a>).</p>

