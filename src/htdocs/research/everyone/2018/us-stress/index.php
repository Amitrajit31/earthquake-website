<p>
  Earthquakes near <a href="/learn/glossary/?term=plate%20tectonics">tectonic plate boundaries</a> are caused by the <a href="/learn/glossary/?term=stress">stress</a> of plate motions. It is less obvious why there are earthquakes in the middle of the plates, far away from plate boundaries. The simplest explanation is that these earthquakes, too, are controlled by the distant plate boundaries; but a new U.S. map of stress in the Earth’s crust challenges that idea.
</p>
<p>
  Researchers took advantage of a large new dataset that they used to analyze earthquakes in the middle part of the U.S., in the deep interior of the North American plate. The new data is available because of both the increase in <a href="/research/induced/">human-induced earthquakes</a> in parts of the Central U.S. and additional (and better) seismic instruments to record induced and natural seismicity.  The nationwide dataset they used included 3,848 earthquakes; 1,966 in the Central U.S.  This is 7 times more data than were used in the most recent stress map database.
</p>
<p>
  The scientists determined the direction and style (<a href="/learn/glossary/?term=fault">normal, strike-slip, or thrust</a>) of slip from each earthquake, and by combining many earthquakes, calculated the prevailing crustal stress in that location. The new map of crustal stress across the U.S. shows that the plate interior stress is variable, with contributions from plate boundary stress, crustal collapse due to gravity, and more local and subtle changes to style, orientation, and earthquake rate. These variations appear to reflect persistent stress conditions, existing for millions of years, possibly caused by differences in the crust inherited from long-dead tectonic events including mountain-building and continental rifting.
</p>

