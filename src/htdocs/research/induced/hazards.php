<h2>Hazard Estimation</h2>
<p>
  The <a href="/hazards/"></a>USGS National Seismic Hazard Model (NSHM) forecasts earthquake hazard, or the potential strength and frequency of ground shaking from future earthquakes, in the continental United States. The USGS releases a forecast about every six years, the <a href="/hazards/hazmaps/conterminous/index.php#2014">last one was released in 2014</a>. The forecasts are used in the design of buildings, bridges, highways, and other structures. They also provide critical information about areas of higher earthquake hazard for use by governmental disaster management agencies, industry, and the public for use in developing <a href="http://www.earthquakecountry.org/booklets/">earthquake risk reduction plans and actions</a>.
</p>
<p>
  In previous NSHM forecasts, earthquakes that were attributed to human activity, including induced earthquakes from underground fluid injection or extraction, were not included. However, because of the recent increase in induced earthquakes in some areas of the central and eastern United States (CEUS) (e.g., Oklahoma) (Ellsworth, 2013) (Figure 1) and because the largest induced earthquakes have caused damage to buildings and other structures, induced earthquakes now need to be considered. Most induced earthquakes in the CEUS are thought to be caused by deep wastewater-disposal related to industrial activity (Andrews and Holland, 2015).
</p>

<h2>One-Year Forecasts</h2>
