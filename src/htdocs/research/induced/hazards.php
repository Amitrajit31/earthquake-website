</figure>
<p>
  In March 2017, the USGS released an updated induced earthquake forecast for the CEUS for 2017. The <a href="/hazards/hazmaps/conterminous/index.php#2017">2017 one-year forecast</a> uses the same earthquake hazard model and methodology as the 2016 one-year forecast, but incorporates an updated earthquake catalog that includes earthquakes from 2016 (Figure 3). The 2017 forecasted earthquake rates are lower in regions of induced earthquakes due to lower rates of earthquakes in 2016 compared to 2015, which may be related to decreased wastewater injection, caused by regulatory actions or by a decrease in unconventional oil and gas production. Nevertheless, the 2017 forecasted earthquake hazard is still significantly elevated in areas of the CEUS compared to the earthquake hazard calculated from only natural earthquakes in the 2014 NSHM.
</p>

<h2>Continuing Research</h2>
<p>
  Current research activities include: development of new ground motion models (GMMs) specifically for induced earthquakes, analysis of catalog statistics, assessment of differences in induced and natural earthquake sources, estimation of potential induced earthquake magnitudes, consideration of alternative seismic rate models that are constrained by physics, and tests of models to identify parameters are all areas where research could improve the methodology. The USGS welcomes feedback from industry, academia, and users of the forecasts as this work continues.
</p>

