      Jello can be an marvelously visual (and tasty) tool to help children understand the difficult concept of waves.  Cover a pan of jello with plastic wrap and build buildings with wood blocks or Legos to place on top of it.  One can then show how an &quot;earthquake&quot; (tapping the side of the pan) can knock down a building somewhere else as the waves (clearly visible in the wobbly jello) carry the energy to the blocks.  A nice added touch is to point out how reinforced buildings (the Legos) do a better job of resisting the shaking than unreinforced buildings (wooden blocks).  At the end, take off the plastic wrap, let the children eat the jello and they will remember that lesson for years.
    </p>
    <p>
      If you wish to use this experiment, here are some tips to ensure its success. First, the jello must be extra stiff, with double the normal amount of gelatin (&quot;Knox Blocks&quot; or &quot;Jello Jigglers&quot;).  I use fruit juice with 1 package of Knox gelatin for each cup of juice.  Second, use a pan with weak sides.  I usually use a disposable aluminum pan.  If you use a pan with rigid sides, such as glass, you could hit the sides with a hammer and still not make much of a wave in it.  Third, the jello should come close to the top of the pan.  I use 6 cups of juice (and 6 packets of gelatin) for an 8"x8" pan which puts it within inches of the top of the pan.  This helps the waves to form and makes them more visible.
