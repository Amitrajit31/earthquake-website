<p> Scientists cannot predict precisely when the next 1906-like earthquake
will happen. Unfortunately, earthquakes do not produce known warning signs
just before they occur. So estimates of when a large earthquake will occur
are imprecise and are based on "models." </p>

<p> A model is a simplified idea of how something works. For the repeat
of the 1906 earthquake, the best models consist of two parts: the THEORY
of plate motion and the accumulation of stress along a locked fault, and
OBSERVATIONS of past earthquakes on that fault and the rate the plates
are now moving. These models suggest that it might take 200 years or more
(starting in 1906) before enough stress accumulates on the fault to produce
another great earthquake. <em> (The long-term rate of motion, averaged
over many earthquake cycles, on the 1906-segment of the San Andreas fault
is between 3/4 to 1 inch per year. At this rate, a 20 ft earthquake offset
requires 200-240 years to accumulate.) </em> </p>

<p> But this estimate is not precise because earthquakes do not occur "like
clockwork", and because other factors not included in our simple model
may be involved. So, while the most likely time for a 1906-like earthquake
to strike again is perhaps late in the next century, there is a small
chance (about 2 percent) that it could happen in the next 30 years. </p>

<p> <b>However, this does not mean that the San Francisco Bay region will
not experience damaging earthquakes in the next hundred years. </b> </p>

<p> Models for the Hayward fault, the Rogers Creek fault and the Peninsula
section of the San Andreas fault suggest that smaller earthquakes (say,
ones with magnitude 7) are also expected, and are now much more likely
to cause serious damage than a repeat of the Magnitude 8, 1906 earthquake. </p>

<p> Shaking is just as intense in an M7 as it is in an M8, but it doesn't
last as long and isn't as widespread. Main danger now to Bay area is from
M7's. </p>

<p> If the 19th century is an example, then we can expect one or more M7
earthquakes on Bay area faults in the decades leading up to the repeat
of the 1906 earthquake -- i.e., over the next several decades.</p>

