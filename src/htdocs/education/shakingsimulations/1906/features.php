          <h2>Spatial Variations in Shaking Intensity</h2>

          <p>
  Note that the rupture front simultaneously propagates
  to the north and south along the San Andreas fault
  away from the epicenter.  The character of the shaking
  at any location is influenced by the amount of slip on
  nearby sections of the fault, properties of the
  geologic materials that the waves passed through to
  reach the location, and especially the local ground
  conditions at the location.
          </p>
          <p>
  Sites on bedrock are often less susceptible to strong
  shaking than sites located on unconsolidated
  sediments.  In particular, thick accumulations of
  deposits in sedimentary basins tend to amplify
  shaking, not unlike a bowl of jello.  For example,
  Santa Rosa experienced heavy damage in 1906 and it can
  be seen in the simulation that high intensities of
  shaking are predicted by the computer model.
  These high intensities in the simulations result from
  sedimentary deposits under Santa Rosa, which were included in the
  the 3-D Geologic Map used as input to the model.
  Much of the character in the
  final shaking intensities offshore can also be
  attributed to the presence of offshore sedimentary
  basins.
          </p>

