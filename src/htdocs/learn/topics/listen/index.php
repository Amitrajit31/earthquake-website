<p>
	When we listen to music we hear a wide range of sound frequencies
  or notes. When we feel an earthquake we feel a wide range
  of frequencies but unlike music the notes are less distinct.
  People describing what they felt during an earthquake may
  call it a "sharp jolt" or a "rolling motion." The sharp jolt
  is due to high frequencies while the rolling motion is due
  to low frequencies. What you feel during an earthquake is
  a product of the earthquake itself, how far you are from the
  earthquake, and the types of rocks between you and the earthquake.
  To learn about this we will convert the shaking during earthquakes
  to sound and then listen to the sounds.
</p>
<h3>Converting Shaking to Sound</h3>
<p>
  When a loudspeaker produces sound it shakes. During an earthquake
  the ground shakes. To convert the earthquake shaking to sound
  we make the loudspeaker shake the same way the ground did. But,
  if we really did that the sound would be too low for people
  to hear. So, we speed things up so that the loudspeaker moves
  back and forth much faster than the ground did and then we can
  hear it.
</p>
