  <p>This map shows the capability of the ground
    to amplify earthquake shaking in the communities of Alameda,
    Berkeley, Emeryville, Oakland, and Piedmont. The National Earthquake
    Hazards Reduction Program recognizes 5 categories of soil types
    and assigns amplification factors to each. Type E soils in general
    have the greatest potential for amplification, and type A soils
    have the least. These soil types are recognized in many local
    building codes. Records from many earthquakes show that ground
    conditions immediately beneath a structure affect how hard the
    structure shakes. For example, sites underlain by soft clayey
    soils tend to shake more violently than those underlain by rock.
    The map depicts the amplification potential at a regional scale,
    and it should not be used for site specific design. Subsurface
    conditions can vary abruptly and borings are required to estimate
    amplification at a given location.</p>
