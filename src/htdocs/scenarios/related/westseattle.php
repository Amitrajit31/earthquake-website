<h2>Abstract</h2>

<p>
	A shallow bedrock fold imaged by a 1.3-km long high-resolution
  shear-wave seismic reflection profile in West Seattle
  focuses seismic waves arriving from the south. This focusing
  may cause a pocket of amplified ground shaking and the anomalous
  chimney damage observed in earthquakes of 1949, 1965 and
  2001. The 200-m bedrock fold at ~300-m depth is caused by
  deformation across an inferred fault within the Seattle fault
  zone. Ground motion simulations, using the imaged geologic
  structure and northward-propagating north-dipping plane wave
  sources, predict a peak horizontal acceleration pattern that
  matches that observed in strong motion records of the 2001
  Nisqually event. Additionally, a pocket of chimney damage
  reported for both the 1965 and the 2001 earthquakes generally
  coincides with a zone of simulated amplification caused by
  focusing. This study further demonstrates the significant
  impact shallow (&lt;1km) crustal structures can have on earthquake
  ground-motion variability.
</p>

<h2>Description of Simulation</h2>
<p>
	Time slices for plane wave
  approaching from southern (left) side of bedrock fold model.
  The plane wave is dipping 45&deg; north. Interpreted geologic
  boundaries are black and white solid lines. S-wave velocities
  for three interpreted layers are (shallowest to deepest) 485
  m/s, 725 m/s and 1200 m/s. Seismograph stations HOLY and WEK
  are shown at top of model. WEK experienced a nearly three-fold
  greater amplification in horizontal peak ground acceleration
  than HOLY during the 2001 Nisqually earthquake. The direct
  SH-wave is initially arched by bedrock-sediment interface (white
  geologic boundary). As simulation progresses, the focused direct
  SH-amplitude increases. The highest wave amplification occurs
  when focused segment of direct SH-wave reaches region of station
  WEK.
</p>

