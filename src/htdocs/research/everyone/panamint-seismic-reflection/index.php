    <p>
      To get a picture of what’s going on 500 ft below the surface, the team of scientists will use 150 geophones and a seismic source.  The geophones are extremely sensitive sensors that will record the shaking produced by the seismic source. The seismic source, or “mini-vibe,” is a 3-ton vibrator pulled on trailer behind a truck.  It shakes the ground enough for the sensors to record the signal, but 20 ft beyond the mini-vibe, a person can barely feel the shaking.
    </p>
    <p>
      The scientists will push the 150 geophones 3 inches into the ground along the side of the road, and then the mini-vibe will crawl down the road, stopping every 15 ft to shake the ground. When the crew reaches the end of the line of geophones, they’ll remove them and place them along the next 1-mile stretch, and then continue with the mini-vibe.  The survey will cover a total of about 3.5 miles by shaking, recording, and then leap-frogging with the geophones.  When all the data is put together from all the shakes at all the geophones, the result is a seismic reflection profile, a picture of the subsurface much like an ultrasound. Scientists hope the seismic-reflection “pictures” will reveal the position of the faults below the surface.
    </p>

