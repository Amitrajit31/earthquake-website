          <p><strong><a name="adults" id="adults"></a>For Adult Readers</strong></p>
          <p><strong>Nature of the Stratigraphical Record</strong>, by Derek V. Ager <br />
  Age: Adult/Young Adult. Paperback: ISBN 0471938084 <br />
  Referred to as a provocative book of commentary on the general pattern of earth history. Twenty years after publication, it still drives us to try and understand the how and why of the sedimentary rock record.<br />
          </p>
          <p><strong>Earthquakes and Geological Discovery</strong>, by Bruce A. Bolt<br />
  Age: Adult/Young Adult. Hardcover: ISBN 10403213<br />
  Describes how and why scientists trace seismic activity and measrue the extent and patterns of seismic waves.<br />
          </p>
          <p><strong>Wonderful Life: The Burgess Shale and the Nature of History</strong>, by&nbsp;Stephen J. Gould <br />
  Age: Adult. Paperback: ISBN 039330700X, Hardcover: ISBN 0735100314 <br />
  This book is about the Cambrian explosion of life and the Burgess Shale. <br />
          </p>
          <p><strong>Time's Arrow, Time's Cycle: Myth and Metaphor in the Discovery of Geological Time (Jerusalem-Harvard Lectures)</strong>, by Stephen J. Gould <br />
  Age: Adult. Paperback: ISBN 0674891996 <br />
  This book is about geologic time and the history of geology. <br />
          </p>
          <p><strong>This Dynamic Earth: The Story of Plate Tectonics</strong>, by Jacquelyne Kious, Robert I. Tilling<br />
  Age: Adult/Yound Adult. Paperback: ISBN 0160482208 or online at HYPERLINK http://pubs.usgs.gov/publications/text/dynamic.html http://pubs.usgs.gov/publications/text/dynamic.html<br />
  An excellent USGS publication with lots of great pictures! Includes information about the discovery and science of plate tectonics.<br />
          </p>
          <p><strong>Why the Earth Quakes: The Story of Earthquakes & Volcanoes</strong>, by&nbsp;Matthys Levy, Mario Salvadori <br />
  Age: Adult. Paperback: ISBN 0393315274, Hardcopy: 0393037746 <br />
  The authors examine famous instances of exploding volcanoes and devastating earthquakes, reliably giving the figures on casualties and Richter magnitudes and leavening the grimness with notes about why, for example, a Frank Lloyd Wright hotel withstood the 1923 Tokyo earthquake.<br />
          </p>
          <p><strong>Control of Nature</strong>, by John McPhee <br />
  Age: Adults/Young Adult. Paperback: ISBN 0374522596 <br />
  This book describes three settings-Iceland; Louisiana; and Los Angeles-where people are trying to control nature and winning or losing to various degrees. McPhee tells the essential human side of the drama to answer the questions: Why are we doing this and Can we win? <br />
          </p>
          <p><strong>Basin and Range</strong>, by John McPhee <br />
  Age: Adult/Young Adult. Paperback: ISBN 0374516901, Hardcover: ISBN 0374109141 <br />
  This book shows how rocks seen in road cuts can tell us about the history of North America. It is an excellent introduction to the theory of plate tectonics. Part of the Annals of the Former World Collection. <br />
          </p>
          <p><strong>In Suspect Terrain</strong>, by John McPhee <br />
  Age: Adult/Young Adult. Paperback: ISBN 0374517940, Hardcover: ISBN 0374176507 <br />
  Interesting book about the complex geology of the Delaware Water Gap area. Examines the ability of current theories to explain the geology. Part of the Annals of the Former World Collection. <br />
          </p>
          <p><strong>Rising from the Plains</strong>, by John McPhee <br />
  Age: Adult/Young Adult. Paperback: ISBN 0374520658, Hardcover: ISBN 0374250820 <br />
  Well written book which interweaves the geologic history of Wyoming with stories of contemporary Wyoming life. Part of the Annals of the Former World Collection. <br />
          </p>
          <p><strong>Assembling California</strong>, by John McPhee <br />
  Age: Adult/Young Adult. Paperback: ISBN 0374523932, Hardcover: ISBN 0374106452 <br />
  A discussion of the geological history of California, both natural and man-made. Part of the Annals of the Former World Collection.<br />
          </p>
          <p><strong>The Earth in turmoil; earthquakes, volcanoes, and their impact on humankind</strong>, by Kerry Sieh <br />
  Age: Adult/Young Adult. Paperback: ISBN 0716736519, Hardcover: ISBN 0716731517<br />
  Outlines the latest tectonic theories and analyzes some key geological events in detail.<br />
          </p>
          <p><strong>The Geology of Earthquakes</strong>, by Robert S Yeats, Kerry Sieh, Clarence R. Allen<br />
  Age: Adult/Young Adult. Hardcopy: ISBN: 0195078276<br />
  Describes the elements of geology that are fundamental to earthquakes.<br />
          </p>
          <p><strong>Roadside Geology</strong> , author varies by book <br />
  Age: Adult/Young Adult. Format and ISBN depends on the state. <br />
  Written by local experts, this wonderful collection of books contains accessible information on state geology, especially that visible from major highways. Almost every state is represented by an individual book (i.e. The Roadside Geology of Utah). Don't do a road trip without it!</p>
