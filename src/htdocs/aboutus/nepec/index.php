<p>
  The National Earthquake Prediction Evaluation Council (NEPEC) was created in 1980 by legislation reauthorizing the <a href="http://www.nehrp.gov/">National Earthquake Hazards Reduction Program (NEHRP)</a>. The Council provides advice and recommendations to the Director of the U.S. Geological Survey on earthquake predictions and related scientific research, in support of the Director&rsquo;s delegated responsibility under NEHRP to issue timely warnings of potential geologic disasters. The Council&rsquo;s membership and procedures are compliant with the <a href="http://www.epic.org/open_gov/faca.html">Federal Advisory Committee Act (FACA)</a>. NEPEC meetings are announced on this web site, and also published in the Federal Register in compliance with the notification requirement of section 10(a)(2) of the FACA. However, as stated in P.L. 101-614, the Council shall be exempt from the notification requirement when meeting on an urgent matter.
</p>

