<p>
  What if the shaking from an earthquake was irrelevant because all our buildings were safe from collapse, and they could keep us safe inside them? That’s one of the goals earthquake engineers are trying to accomplish. USGS earthquake engineer Mehmet &Ccedil;elebi and several other colleagues in other agencies recently studied the “before” and “after” of a 55-story tall building in Osaka, Japan that was subjected to two real earthquakes, once before a seismic retrofit and again after the retrofit. This was a rare case that offered a rare opportunity to study the retrofit “before” and “after” of a tall building shaken by two distant earthquakes. The scientists wanted to know how much safer the engineering improvements had made the building for the type of potentially-damaging shaking that is caused by large distant earthquakes.
</p>
<p>
  When an earthquake occurs, energy is radiated out in all directions through the earth’s crust as seismic waves that we feel as shaking.  The shaking behavior includes fast shaking motions like a toddler shaking a rattle, as well as slower shaking that’s more like an ocean swell.  For our purposes here, there are two important facts that are relevant: 1) the fast-shaking (called short-period) energy are dominant  near the earthquake location and tend to die out before they travel very far, but the slow-shaking (long-period) energy is able to travel very far in the earth’s crust before it dies out; and 2) tall buildings are affected more by the long-period energy, and shorter buildings are affected more by the short-period energy.
</p>
