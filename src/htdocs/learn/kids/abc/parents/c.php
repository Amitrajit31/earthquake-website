    <h2>Core</h2>
    <p>
      Earthquakes are not just a source of destruction. To seismologists, they provide a way of &quot;seeing&quot; into the inside of the Earth. Waves produced by earthquakes travel through the Earth and bounce off different features of the Earth's interior. The patterns they form after bouncing off these features can be used to image the interior, much like a doctor's CAT scan provides a picture of the interior of our bodies.
    </p>
    <p>
      The reflection of seismic waves showed seismologists that the center of the Earth is composed of iron and nickel. This metallic core has two parts, the outer core (from 2500 miles to 3500 miles below the surface) where the metal is liquid and the inner core (the central 500 miles of the Earth) where the great pressures force the metal back into a solid state. The core was initially recognized by the first prominent woman seismologist, Inge Lehmann (see <a href="i.php">Inge</a>).
    </p>
    <h2>Crust</h2>
    <p>
      The outermost layer of the Earth is called the crust. The thinnest crust at the mid-ocean ridges is 3 to 5 miles thick and the thickest crust under the Himalayas and Tibet is as much as 60 miles thick. The average thickness under the continents is about 20 miles. Because earthquakes only happen where the rocks are cold enough to be brittle, almost all of them occur in the crust. The boundary between the crust and the mantle that lies beneath it is called the Mohorovic discontinuity, or Moho for short, named after the renowned seismologist, A. Mohorovic, who discovered it in 1908. The difference between the crust and mantle is chemical, with different, denser rocks in the mantle. The different rocks transmit seismic waves at different speeds causing reflections that allowed Dr. Mohorovic to recognize the Moho (see <a href="c.php">Core</a>).
    </p>
