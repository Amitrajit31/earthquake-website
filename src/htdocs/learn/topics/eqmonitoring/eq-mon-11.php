          </p>
          <p><strong>Planet Earth, Inside Out</strong> , by Gail Gibbons <br />
  Grades K&ndash;3. Paperback: ISBN 0688158498, Hardcover: ISBN 0688096808 <br />
  A simple, clear introduction to earth science, covering Earth's genesis, composition, and continuing morphology. Good descriptions of complex processes. <br />
          </p>
          <p><strong>Earth (Connections)</strong>, by Caroline Grimshaw, Iqbal Hussain, Nick Duffy, Spike Gerrell <br />
  Grades 4&ndash;7. Paperback: ISBN 0716613050, Hardcover: ISBN 1568474539 <br />
  Deals with basic geoscience questions such as the origin of Earth and geological/biological changes through time. <br />
          </p>
          <p><strong>Big Rock</strong>, by Bruce Hiscock <br />
  Grades 4&ndash;7. Paperback: ISBN 0689829582, Hardcover: ISBN 0689314027 <br />
  Looks at how a granite rock near the author's house reveals information about Earth history. <br />
          </p>
          <p><strong>Eyewitness Explorers: Rocks & Minerals</strong>, by Steve Parker, Peter Visscher, Raymond Turvey, N. J. Hewetson <br />
  Grades K&ndash;3. Paperback: ISBN 0789416824 <br />
  Kids can learn about fossils as well as the properties of common rocks and minerals. <br />
          </p>
          <p><strong>Understanding Earth (2nd Edit): No Stone Unturned : Reasoning About Rocks and Fossils</strong> , by Frank Press, Raymond Siever, E. K. Peters <br />
  Grades 9&ndash;Adult. Paperback: ISBN 0716731339 <br />
  An unbeatable introductory text written by masters of both education and earth science. <br />
          </p>
          <p><strong>Earthquakes</strong>, by Seymour Simon <br />
  Grades K&ndash;3. Paperback: ISBN 068814022X, Hardcover: ISBN 0688096344 <br />
  All about earthquakes and their interactions with manmade structures. <br />
          </p>
          <p><strong>Rocks and Minerals (Eyewitness Books)</strong>, by R. F. Symes, Colin Keates <br />
  Grades 4&ndash;7. Hardcover: ISBN 0394896211 <br />
  Full color pictures and interesting text about the formation and uses of rocks and minerals. <br />
          </p>
          <p><strong>Stone Wall Secrets</strong>, by Kristine Thorson, Robert Thorson, Gustav Moore <br />
  Grades 4&ndash;7. Hardcover: ISBN 0884481956 <br />
  A farmer shares the geological history of the stones in a stone wall around the family's property with his grandson. <br />
          </p>
          <p><strong>Volcano and Earthquake (Eyewitness Books number 38)</strong>, by Susanna Van Rose, James Stevenson <br />
  Grades 4&ndash;7. Hardcover: ISBN 0679816852 <br />
  Book discusses where and how earthquakes and volcanoes occur, prediction, and human interaction with these natural phenomena. <br />
          </p>
          <p><strong>Janice VanCleave's Earth Science for Every Kid: One Hundred & One Experiments That Really Work</strong>, by Janice Vancleave <br />
  Grades K&ndash;6. Paperback: ISBN 0471530107 <br />
  A terrific collection of earth science experiments for kids.<br />
          </p>
          <p><strong>Janice VanCleave's Earthquakes: Mind Boggling Experiments You can Turn into Science Fair Projects</strong>, by Janice Vancleave <br />
  Grades 4&ndash;7. Paperback: ISBN 0471571075 <br />
  20 simple experiments explaining earthquakes that kids can do with materials found in the home. <br />
          </p>
          <p><strong>Planet Earth</strong> , by Fiona Watt, Corinne Stockley, Kuo Kang Chen <br />
  Grades 6&ndash;Adult. Paperback: ISBN 0746006373, Hardcopy: ISBN 0881105104 <br />
  One of the best elementary earth science books available.<br />
