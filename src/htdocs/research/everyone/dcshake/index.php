<h2>Washington DC Stone and Brick Buildings Vulnerable to Distant Quakes</h2>
<p><i>
  New study shows that DC geologic conditions strongly affect earthquake shaking.
</i></p>

<figure class='right wa-mon'>
  <img src="images/wa-monument.jpg" alt=""/>
  <figcaption>
    Photo of the pyramid top of the Washington Monument from the U.S Park Police Helicopter. (photo courtesy of the National Park Service)
  </figcaption>
</figure>

<br style="clear:left;"/>

<p>
  On August 23, 2011 a <a href="https://earthquake.usgs.gov/earthquakes/events/2011virginia/">magnitude 5.8 earthquake in Mineral, Virginia</a> rattled Washington, DC, 130 km (81 mi) away, and caused some tens of millions of dollars in widely-publicized damage.  Prominent among the damaged buildings and structures were concentrated damages to the pyramid top of the Washington Monument; the Sherman Building at the Armed Forces Retirement Home; parts of the Washington National Cathedral such as the flying buttresses and spires; and the chimneys and other small parts of the Smithsonian “Castle”.  Minor damage around the city included cracked masonry and chimneys, other broken architectural elements, and toppled gravestones.
</p>

<h3>How could a moderate-sized earthquake so far away cause so much damage?</h3>

<figure class='left dyfi'>
  <img src="images/felt-comparisons.gif" alt=""/>
  <figcaption>
    An earthquake of the same size in the Eastern U.S. is felt more widely than one in the Western U.S. because seismic waves travel further in the colder, denser crust of the Eastern U.S.
  </figcaption>
</figure>

<p>
  Older, denser crustal rocks allow earthquake wave energy to travel greater distances in the central and eastern parts of the United States than through the younger, less consolidated rocks in the western part of the country.  It’s also well known that older stone and brick buildings and other unreinforced structures, which are more prevalent in the east than in the west, are more susceptible to shaking damage from an earthquake.  But even so, the observed damages in some of the structures, and even the <a href="/earthquakes/eventpage/se609212#dyfi">citizen-science reports in the “Did You Feel It?” system</a>, indicated stronger shaking in Washington, DC, than would be expected from the Virginia earthquake.
</p>

<br style="clear:left;"/>

<div class='sidebar'>

  <h3>Amplifications, frequencies, resonant frequencies… huh?</h3>
  <p>
    Let’s spend a bit more time on these concepts. An earthquake releases energy as seismic waves, like a pebble dropped into a pond causes waves on the surface of the water, but seismic waves travel through the earth, not just on the surface.  Seismic waves shake the ground they travel through at many different <a href="/learn/glossary/?term=frequency"><strong>frequencies</strong></a> at the same time – fast shaking, slow shaking, medium-shaking.
  </p>
  <p>
    The <a href="/learn/glossary/?term=amplitude"><strong>amplitude</strong></a>, or how far the ground moves back-and-forth during the shaking, can be increased, or amplified, by many different factors. In the case of the DC area, when the seismic energy moves from solid bedrock into overlying, thin layers of less-dense, less-consolidated sediment, the seismic waves get <a href="/learn/glossary/?term=amplification"><strong>amplified</strong></a> Because the energy moves the weaker sediments farther than the stronger bedrock.
  </p>

  <figure class='right'>
    <img src="images/eqbuilding.gif" alt="A sketch of a short building and a tall building with a high frequency waveform beneath the short one and a low frequency waveform beneath the tall one."/>
  </figure>

  <p>
    The coolest part is that different buildings, parts of buildings, and other structures have a <a href="/learn/topics/buildings-eqs.php"><strong>resonant frequency</strong></a>.  That is, there is a frequency of shaking to which the structure responds more than other frequencies. When the resonant frequency of the structure matches the ground shaking frequency, the chances of damage increase. So, for example, tall buildings tend to have resonant frequencies that make them more susceptible to damage during slow shaking, which enhances the natural swaying of the building, whereas if the shaking below the tall building is fast the building will be less affected.  The opposite is true for shorter buildings – fast ground shaking tends to induce and amplify their natural shaking, whereas slow shaking has little effect on the building.
  </p>

</div>

<h3>What caused the increased ground motion in the DC area?</h3>
<p>
  That’s exactly what Thomas Pratt and his colleagues aimed to find out when they installed 27 temporary seismic instruments around the DC area in late 2014. Even though there are few earthquakes in that area, the sensitive instruments recorded 30 earthquakes in the U.S. and around the world during the 10 months they were in place.
</p>

<figure class='right map'>
  <img src="images/sta-geol-map.gif" alt=""/>
  <figcaption>
    Map of Washington, DC, showing Piedmont rocks (pink) and ACP strata and other unconsolidated deposits (yellow) (Darton, 1950; Southworth &amp; Denenny, 2006; Powars, Catchings et al., 2015, Powars, Edwards et al., 2015). The red dots show seismometer sites, the black shapes show major buildings, and the rivers are gray. AFRH = Armed Forces Retirement Home (Sherman building); CA = Capitol; LM = Lincoln Memorial; NA = National Airport; NC = Washington National Cathedral;PG = Pentagon; WA = Washington Monument; WH = White House. The red line is a cross section. (from Pratt et al., 2017)
  </figcaption>
</figure>

<p>
  When they examined the recordings of the earthquakes, they discovered that shaking in some parts of the DC area was <strong>amplified</strong> due to large differences between the thin layer of sediments under parts of the city compared to areas built on more solid, harder bedrock.  In many cases, the parts of the buildings that were most damaged had <strong>resonant</strong> frequencies that matched the frequencies that were amplified at that location, resulting in greater damage.
</p>

<figure class='left'>
  <img src="images/waveforms.gif" alt=""/>
  <figcaption>
    Recordings of a magnitude 3.0 earthquake beneath western North Carolina at two bedrock sites (DC01 and 50 DC02) and two sites on moderately thick sediments (DC05 and DC29). Note the larger amplitudes and longer durations of the recordings at sites on the sediments.  (from Pratt et al., 2017)
  </figcaption>
</figure>


<p>
  The Washington Monument is a particularly interesting case. An engineering study found that the resonant frequency of the 169-meter tower is about 0.3 Hz, but the pyramid at the top has a resonant frequency of about 2.5-5 Hz (faster shaking compared to the tower). You guessed it, the shallow deposits beneath the monument amplified ground motions at frequencies that nearly matched that of the top pyramid where most of the damage occurred.  Similar resonant frequencies in the Smithsonian “Castle” elements resulted in the damage to specific parts such as the flying buttresses and spires.
</p>

<h3>Can shaking amplification happen in other areas?</h3>
<p>
  There are many other central and eastern North American cities sitting entirely or partially on sedimentary layers underlain by older, stronger bedrock similar to the geologic conditions in Washington, DC.  The scientists who performed this research presume that ground shaking amplification can occur in these other locations too.  Some of these locations include Trenton, NJ, Wilmington, DE, Baltimore, MD, Richmond, VA, and Columbia, SC. Similar deposits also underlie cities in the Mississippi Valley and Gulf Coast of the U.S., notably Memphis, TN.
</p>
<p>
  Shaking amplification due to soft sediments, combined with the long distances seismic waves travel in eastern North America and the large numbers of older, more fragile buildings could result in increased earthquake risk for much of the eastern U.S. and Gulf Coast regions.
</p>

