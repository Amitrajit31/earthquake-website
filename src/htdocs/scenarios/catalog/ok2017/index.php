<h2 id="description">Description</h2>

<p>
  This catalog includes a single earthquake scenario that was requested by the
  Oklahoma County, Oklahoma, Local Emergency Planning Committee (OKCLEPC) in
  support of their earthquake drill on 10-12-2017.  The OKCLEPC requested that
  the scenario event be located near their exercise focus area in Jones,
  Oklahoma.  The hundreds of earthquakes (magnitude 2.5-4.0) that have occurred
  near Jones over the past 7 years makes a scenario like this plausible.
</p>

<p>
  A scenario represents one realization of a potential future earthquake by
  assuming a particular magnitude, location, and fault-rupture geometry and
  estimating shaking using a variety of strategies. 
</p>

<p>
  In planning and coordinating emergency response, utilities, local government,
  and other organizations are best served by conducting training exercises based
  on realistic earthquake situationsâ€”similar to those that have a reasonable
  chance of occurring. ShakeMap Scenario earthquakes can fill this role. They
  can also be used to examine exposure of structures, lifelines, utilities, and
  transportation corridors to specified potential earthquakes.
</p>


<h3 id="event-selection">Event Selection</h3>

<p>
  The earthquake source parameters were selected based on seismicity trends near
  Jones from 2009 to 2017, consideration of earthquake source parameters from the
  2011 M5.7 Prague, Oklahoma, and the 2016 M5.8 Pawnee, Oklahoma, earthquakes,
  fault information provided by the Oklahoma Geological Survey, and discussions
  among Rob Williams (USGS), Dan McNamara (USGS), Andy Michael (USGS), Jacob
  Walter and Jerry Boak (Oklahoma Geological Survey), David Wald (USGS), and
  Eric Thompson (USGS).
</p>


