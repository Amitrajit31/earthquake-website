<p>
  The next, and arguably most important, step is to submit the radiocarbon and OSL (optically stimulated luminescence) samples (see Day 13 for details) to the laboratories. For the radiocarbon samples, a lab in Massachusetts will separate out the bits of charcoal and identify the plant species they come from. We try to avoid plant sample types like hardwood because it can stick around in the landscape for long periods of time and is less precise for dating. The OSL samples will be be processed in Denver at a USGS lab. The sediment will be sieved down to fine sand and separated so only the quartz minerals remain, then analyzed for the luminescence signal. The radiocarbon results usually take several months (or more) until age results are returned, and the OSL can take about 6 month to a year for results.
</p>
<p>
  The age results are a very important part of the data collection because they will constrain when the earthquakes occurred. We can use these data (along with data from other trenches) to determine how often these large (greater than magnitude 6.5) earthquakes occur, how much vertical slip they generated, and which parts of the fault are rupturing in each event. For example, if 3 different trenches show evidence for the same earthquake, we can estimate that the rupture length was large enough to span all 3 sites. If an earthquake only shows up in one trench, it may have been a much shorter rupture length. Ultimately, the rupture length along the fault is related to the earthquake magnitude and how much shaking it produces. Information from studies like this inform seismic hazard maps, which are used to guide building codes in areas that might experience strong shaking. You can read our <a href="/education/paleo-intro/">Introduction to Paleoseismology</a> webpage for more details about the whole process.
</p>
<p>
  This study was by far the most comprehensive look at the Teton fault to date. We will combine these results with data from two smaller hand dug trenches near the center and northern end of the Teton fault (dug by the same Teton team), as well as data from a Teton trench dug  about 30 years ago. Modern advancements in technology, such as improved dating techniques, lidar, photography, and 3D modelling have really changed paleoseismic studies for the better over the last few years/decades.
</p>
