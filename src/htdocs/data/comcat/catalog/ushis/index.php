<h3 id="additional">Additional Information</h3>

<p>
  <strong>Citation:</strong> Stover, C.W. and J.L. Coffman, 1993, <a href="https://pubs.usgs.gov/pp/1527/report.pdf">Seismicity of the United States, 1568-1989 (Revised)</a>, U.S. Geological Survey Professional Paper 1527, 417 pp.
</p>

<p>
  This publication is a history of the principal earthquakes in the United States from 1568 through 1989.  The authors define "principal earthquakes" to be events of magnitude >=4.5 or Modified Mercalli Intensity >= VI.  For the State of Alaska and offshore areas of California, Oregon and Washington, the magnitude threshold was increased to M >= 5.5 because of the high seismicity of those regions.
</p>
