<h2>Monitoring History Timeline</h2>

<p><strong>373 B.C.</strong> Earthquake of Helice, Greece</p>
<p><strong>132 A.D.</strong> Chang Heng, seismoscope</p>
<p><strong>1556</strong> Chinese earthquake; 830,000 deaths reported</p>
<p><strong>1707 October 28.</strong> Great Japanese Earthquake</p>
<p><strong>1755 November 1.</strong> Lisbon Earthquake</p>
<p><strong>1783. February 5.</strong> and following. Earthquakes of Calabria, Italy, scientific field investigation</p>
<p><strong>1819 June 16.</strong> Earthquake of Cutch, India; observed faulting (Allah Bund).</p>
<p><strong>1830.</strong> Lyell, <em>Principles of Geology, </em>first edition.</p>
<p><strong>1846.</strong> Robert Mallet; his first general paper on earthquakes.</p>
<p><strong>1855.</strong> Kreil, early seismograph.<br />
    <strong>January 24</strong> - New Zealand earthquake; observed faulting </p>
<p><strong>1857. January 9.</strong> California (Fort Tejon) earthquake; observed strike-slip. <br />
    <strong>December 16.</strong> Earthquake in Italy (Kingdom of Naples); Mallet, field investigation.</p>
<p><strong>1865.</strong> Seismological observations begun at Manila (with seismoscopes; seismographs installed 1881-1889).</p>
<p><strong>1872. March 26.</strong> Earthquake in Owens Valley, California; observed scarps a nd strike-slip.</p>
<p><strong>1879.</strong> Seismographs developed in Japan by the British group.</p>
<p><strong>1880.</strong> Seismological Society of Japan organized.</p>
<p><strong>1883.</strong> August 27. Explosion of Krakatoa.</p>
<p><strong>1884.</strong> Rossi-Forel intensity scale set up. Seismological service established at manila.</p>
<p><strong>1885.</strong> Lord Rayleigh, paper on theory of &quot;Rayleigh waves.&quot;</p>
<p><strong>1887.</strong> Voigt, investigation of elasticity of crystals; definition and naming of tensors. Rudolph, first paper on seaquakes. Seismological stations established in California at Berkeley and Lick Observatory.</p>
<p><strong>1888. September 1.</strong> Amuri earthquake, New Zealand; strike-slip offsetting fences.</p>
<p><strong>1889. April 18.</strong> First seismogram of a teleseim identified.</p>
<p><strong>1891. October 28.</strong> Mino-Owari earthquake, Japan; large strike-slip and dip slip.</p>
<p><strong>1892.</strong> Japanese Imperial Earthquake Investigation Committee established.<br />
    <strong>May 17.</strong> Sumatra Earthquake; strike-slip shown by triangulation survey. </p>
<p> <strong>1894.</strong> New seismographs developed by Vincentini (Padua, Italy) and by Milne in England.</p>
<p><strong>1896.</strong> Seismology Committee of the British Association for the Advancement of Science organization.</p>
<p><strong>1897. June 12.</strong> Great earthquake of Assam, India ; investigation by Oldham.</p>
<p><strong>1898.</strong> Seismograph developed by Omori at Tokyo. Stations with milne instruments initiated at Wellington (New Zealand) and Batavia (Java). </p>
<p><strong>1899.</strong> Knott's equations for reflection and refraction coefficients of elastic waves.<br />
September 3 and 10. Great Alaskan earthquakes, uplifts reaching 47 feet.</p>
<p><strong>1901.</strong> Inverted pendulum seismograph developed by Wiechert. Geophysical Institute founded at Gottingen.</p>
<p><strong>1903.</strong> International Seismological Association organized.</p>
<p><strong>1904.</strong> Seismological station initiated at Uppsala (Sweden).</p>
<p><strong>1906.</strong> Galitzin electromagnetic seismograph developed (Russia). Vertical-component seismograph set up by Straubel and Eppenstein at Jena (Germany). <br />
    <strong>January 21.</strong> Major deep-focus earthquake under Japan.<br />
    <strong>January 31.</strong> Great earthquake, COlumbia and Ecuador.<br />
    <strong>April 18.</strong> California earthquake; great extent of strike-slip faulting; survey re-triangulation leading to elastic-rebound theory of earthquakes</p>
<p><strong>1907.</strong> Seismograph developed by Mainka at Strassburg.</p>
<p><strong>1908. June 30.</strong> Great Siberian meteorite fall.</p>
<p><strong>1909.</strong> Seismological station established at Riverview near Sydney, Australia.<br />
    <strong>October 8.</strong> Earthquake near Zagreb, Croatia; discovery of the subcrustal discontinuity by A. Mohorovicic.</p>
<p><strong>1910.</strong> New instruments at Berkeley.</p>
<p><strong>1911.</strong> Seismological Society of America founded; it's Bulletin began publication. New instruments at Lick OBservatory Station. Galitzin vertical-component seismograph developed (Russia). Theory of Love waves.</p>
<p><strong>1913.</strong> Station established at La Paz, Bolivia. radius of the core determined by Gutenberg at Gottingen.</p>
<p><strong>1915.</strong> Milne-Shaw seismograph developed by J.J. Shaw (England).</p>
<p><strong>1918.</strong> First year covered by <em>International Seismological Summary.</em></p>
<p><strong>1919.</strong> Zoeppritz equations for coefficients of reflection and refraction of elastic waves published (posthumously).</p>
<p><strong>1921.</strong> Carnegie Institution of Washington, Advisory Committee in Seismology appointed.<br />
    <strong>September 21.</strong> Explosion at Oppau, Germany.</p>
<p><strong>1922.</strong> Changes in the variation of latitude and in other geophysical elements.</p>
<p><strong>1923.</strong> Wood-Anderson torsion seismometers began regular recording (Pasadena). </p>
<p><strong>1924.</strong> Nikiforov torsion seismographs in the service (USSR).</p>
<p><strong>1925.</strong> Jesuit Seismological Association organized; headquarters at St. Louis. Seismological work transferred from U.S. Weather Bureau to Coast and Geodetic Survey.<br />
    <strong>June 29.</strong> Santa Barbara earthquake.</p>
<p><strong>1926.</strong> <em>Geophysical Magazine </em>(Tokyo) began publication. Earthquake department of Pacific Board of Fire Underwriters established.</p>
<p><strong>1927.</strong> Seismological Laboratory at Pasadena occupied.<br />
    <strong>March 7.</strong> Tango earthquake (Japan); thoroughly investigated; two fault traces.</p>
<p><strong>1928.</strong> K. Wadati, paper on shallow and deep earthquakes.</p>
<p><strong>1929.</strong> New electromagnetic seismograph developed by Wenner.<br />
    <strong>June 17.</strong> New Zealand Earthquake; expansion of new Zealand seismology followed.</p>
<p><strong>1931.</strong> Variable-reluctance seismometer developed by Benioff (Pasadena). </p>
<p><strong>1932.</strong> Benioff strain seismometer developed. U. S. Coast and Geodetic Survey initiated a program of strong-motion recording in California.</p>
<p><strong>1933. March 3</strong> (Japanese date). Great Sanriku earthquake and tsunami. </p>
<p><strong>1934.</strong> <em>P'P' </em>and relate waves discovered at Pasadena.</p>
<p> <strong>1935.</strong> Magnitude scale published.</p>
<p><strong>1936.</strong> Existence of the inner core suggested by Miss Lehmann in Copenhagen.</p>
<p><strong>1937.</strong> Istituto Nazionale di Geofisica established; headquarters at Rome.</p>
<p><strong>1939. December 27.</strong> Destructive earthquake in Turkey, first of a series extended and connected faulting.</p>
<p><strong>1940.</strong> T wave described and named by Linehan in Weston, Massachusetts.</p>
<p><strong>1944.</strong> <em>Annals de Geophysics</em> began publication.</p>
<p><strong>1946. April 1.</strong> Aleutian tsunami, destructive on Hawaii; seismic sea warning service organized in consequence.<br />
    <strong>July 24.</strong> Atomic bomb test at Bikini; <em>P</em> waves recorded at distant stations.</p>
<p><strong>1947. April 18.</strong> Hegelian demolition.</p>
<p><strong>1948.</strong> C. A. Whitten (U.S. Coast and Geodetic Survey), paper on continuing horizontal displacements in California. </p>
<p><strong>1949.</strong> <em>Annali di Geofisicia </em>began publication.<br />
    <strong>August 6.</strong> Large quarry blast at Corona, California. </p>
<p><strong>1950. August 15.</strong> Great earthquake, Tibet and Assam. </p>
<p><strong>1951.</strong> Strain-release curves developed by Benioff in Pasadena.</p>
<p><strong>1952.</strong> <em>Lg </em>waves discovered by Ewig and Press in at Columbia University, New York.</p>
<p><strong>1953.</strong> <em>Pa </em>and <em>Sa </em>waves discovered by Caloi at Rome and independently by Ewig and Press.</p>
<p><strong>1954. March 29.</strong> Earthquake 640 kilometers deep under Spain.<br />
    <strong>December 16.</strong> Major earthquake in Nevada; large and extensive faulting.</p>
<p><strong>1957. July 1- 1958. December 31.</strong> International Geophysical Year.</p>

