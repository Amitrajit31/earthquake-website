		<b>Spring</b> - US Geological Survey Circular 1188, &quot;Requirements for an
		Advanced National Seismic System&quot; published.
	</li>
</ul>

<h3>1998</h3>
<ul>
	<li>
		<b>June</b> - Workshop in Denver to review status of seismic monitoring attended by network operators,
		engineers and emergency managers.
	</li>
</ul>

<h3>1997</h3>
<ul>
	<li>
		<b>January</b> - Congress passes NEHRP reauthorization (Public Law 105-47) with specific request for the USGS to
		&quot;assess the status, needs and associated costs of seismic monitoring in the United States&quot;
		and authorizes the development of a &quot;real-time seismic hazard warning system&quot;, a precursor to the ANSS.
	</li>
</ul>
