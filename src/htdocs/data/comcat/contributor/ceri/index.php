<h2 id="mission">Mission</h2>
<p>
  The Central &amp; Southeast US Regional Advisory Committee is charged with planning, implementation
  and management for the Central &amp; Southeast US Region of the Advanced National Seismic System (ANSS-MA).
  The Advanced National Seismic System (ANSS) is an effort to upgrade seismic monitoring
  systems throughout the U.S. by improving hardware, management, operations, cooperation,
  and outreach.
</p>
