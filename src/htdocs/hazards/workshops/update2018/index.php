<h2>Workshop Summary</h2>
<p>
  The USGS National Seismic Hazard Model Project (NSHMP) will host a two-day workshop in early March 2018 to solicit feedback from the scientific community on the 2018 update of the National Seismic Hazard Model (NSHM). The workshop will give our partners and interested individuals an opportunity to comment on the inputs to the seismic hazard model, which has historically been adopted into the U.S. Building codes, and affects insurance rate structures, governmental disaster management and mitigation strategies, planning and seismic safety applications, and many site-specific engineering analyses by industries and governments. <strong>The 2018 NSHM update will incude implementation of NGA-East GMMS, basin effects, and an updated seismicity catalog.</strong>
</p>
<p>
  The first day of the workshop will focus on updates to the central and eastern U.S. (CEUS), which focuses on updates to the CEUS ground motion models (GMMs) (i.e., implementation of the NGA-East GMMs). The second day of the workshop will focus on the western U.S. (WUS) and our plan to incorporate basin effects into the NSHM. We will also discuss the update of the smooth seismicity model. The NSHMP invites the scientific community and other interested individuals to attend this workshop to hear about our plans for the 2018 update to the NSHM, discuss the model, and provide valuable feedback to the NSHMP.
</p>

