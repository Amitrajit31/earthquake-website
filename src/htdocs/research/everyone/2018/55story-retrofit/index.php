<p>
  What if the shaking from an earthquake was irrelevant because all our buildings were safe from collapse, and they could keep us safe inside them? That’s one of the goals earthquake engineers are trying to accomplish. USGS earthquake engineer Mehmet &Ccedil;elebi and several other colleagues in other organizations recently studied the “before” and “after” of a 55-story tall building in Osaka, Japan that was subjected to two real earthquakes, once before a seismic retrofit and again after the retrofit. This project offerred a rare opportunity to study the retrofit “before” and “after” of a tall building shaken by two distant earthquakes. The scientists wanted to know how the engineering improvements had changed the response of the building to potentially-damaging shaking that is caused by large distant earthquakes.
</p>
<p>
  When an earthquake occurs, energy is radiated out in all directions through the earth’s crust as seismic waves that we feel as shaking.  The shaking behavior includes fast shaking motions like a toddler shaking a rattle, as well as slower shaking that’s more like an ocean swell.  The fast-shaking (called short-period) energy is dominant near the earthquake location and tends to die out before the waves travel very far, but the slow-shaking (long-period) energy is able to travel very far in the earth’s crust before it dies out. Tall buildings are affected more by the long-period energy, and shorter buildings are affected more by the short-period energy.
</p>
