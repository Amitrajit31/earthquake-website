<h2>Northern California</h2>
<p>
  The Hayward fault in the San Francisco Bay area runs through a densely-populated area, so it has been studied quite a bit.  The most revent major earthquake on this fault was an approximately M6.9 in 1868.  The fault has been creeping about 4.6mm/yr for the last several decades, but that is only half of the long-term slip rate, so stress is building up on this fault.  A paleoseismology study in 2007 at Tyson’s Lagoon (now a BART station) found evidence for 12 paleoearthquakes (including the historical 1868 earthquake) with an average time between earthquakes of about 160 years. The average time interval between the 5 most recent earthquakes is a little shorter, about 140 years. The most recent earthquake occurred in 1868, so the time since then is longer than any of the most recent recurrence intervals.  The study concluded that there is a 33% likelihood of a surface-rupturing earthquake within the next 30 years. (See <a href="https://pubs.usgs.gov/fs/2016/3020/fs20163020.pdf">Earthquake Outlook for the San Francisco Bay Region 2014&mdash;2043</a>)
</p>
<p>
  The Maacama fault is the northward continuation of the Hayward-Rodgers Creek fault system in northern California. In 2014, a paleoseismology site at Hael Creek on the Maacama fault reiterated the results found on the Hayward fault to the south – creeping with infrequent large earthquakes, and a large one expected in the not-too-distant future.
</p>
<p>
  The Hazel Dell site near Corralitos, CA was trenched in 2013 to characterize the Santa Cruz Mountains section of the SAFZ.  The Santa Cruz section stretches 62 km (39 mi) from Los Gatos (near San Jose) to San Juan Bautista, CA and was last ruptured in the famous 1906 San Francisco earthquake.  Observations in the trenches along with radiocarbon dating of charcoal, wood chips, and small plant remains, combined with a reevaluation of three previously-studied nearby paleoseismic sites revealed a variation in seismic activity in the past. Three earthquakes occurred within a 70-year period between 1838 and 1906, but there were no earthquakes during the 500 years before that, and there have been no earthquakes in the 110 years since 1906. This shows that the average time between earthquakes includes some intervals that are short and some intervals that are long. New studies farther to the northwest along the Peninsula section of the SAFZ also show a long interval between the 1906 earthquake and the previous earthquake, which occurred  around 1300. Prior to 1300 the intervals are shorter, about 200 years. The North Coast section of the SAFZ is north of San Francisco. Studies of this section of the fault suggest an average recurrence interval of 200-300 years.
</p>

