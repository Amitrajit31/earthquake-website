<h2 id="mission">Mission</h2>
<p>
  Earthquakes in Hawaii are closely linked to volcanism. They are an important part of the
  island-building processes that have shaped the Island of Hawaii and the other Hawaiian
  Islands. Thousands of earthquakes occur every year beneath the Island of Hawaii.
</p>
<p>
  Eruptions and magma movement within the presently active volcanoes (Kilauea, Mauna Loa
  and Lo`ihi) are usually accompanied by numerous small earthquakes. They originate in regions
  of magma storage or along the paths that magma follows as it rises and moves prior to eruption.
  These are loosely termed volcanic earthquakes.
</p>
<p>
  Many other earthquakes, including the largest ones, occur in areas of structural weakness at the
  base of Hawai`i's volcanoes or deep within the Earth's crust beneath the island. These are referred
  to as tectonic earthquakes. In the past 150 years, several strong tectonic earthquakes (magnitude 6
  to 8) caused extensive damage to roads, buildings, and homes, triggered local tsunami, and resulted
  in loss of life. The most destructive earthquake in Hawai`i's history occurred on April 2, 1868,
  when 81 people lost their lives. With a magnitude of 7.9 and a maximum intensity of XII, this
  destructive earthquake destroyed more than a hundred homes and generated a 15-m high tsunami along
  Kilauea's south coast.
</p>
