<h2 id="mission">Mission</h2>
<p>
  The most seismically active state in the United States, Alaska is the site
  of the second-largest earthquake ever recorded. In 1964, a magnitude 9.2
  earthquake in Prince William Sound caused extensive damage throughout
  south-central Alaska and generated a pacific-wide tsunami.  Seismic
  monitoring for emergency response and hazards mitigation in the State of
  Alaska is a federal-state partnership involving the Unites States
  Geological Survey, National Weather Service, and the State of Alaska
  through the University of Alaska Fairbanks and University of
  Alaska Anchorage.
</p>
<p>
  Comprehensive seismic monitoring of the State of Alaska is achieved by
  integrating seismic monitoring activities/systems at the Alaska Earthquake
  Information Center, University of Alaska Fairbanks, West Coast-Alaska
  Tsunami Warning Center (NOAA) and the National Earthquake Information
  Center, USGS.  The Alaska Earthquake Information Center processes,
  disseminates and archives seismic data and information from more than 300
  seismic sites within the State. Earthquake monitoring and response
  activities are coordinated with WCATWC and NEIC in order to ensure timely
  and authoritative monitoring of the State of Alaska for earthquake hazards
  reduction and mitigation.
</p>
