<p>
  Earthquakes in the central and eastern U.S. tend to cluster in seismic zones rather than being evenly distributed. Most of these highest-hazard zones occur where the local stress patterns differ from the broad, plate-boundary-driven stress. This observation that local stress variations may be the main reason for intracontinental earthquakes helps to explain the <a href="/hazards/hazmaps/conterminous/2014/images/HazardMap2014_lg.jpg">zones of higher central and eastern U.S. seismic hazard</a>. However, work remains to define the exact relationship between the local stress and the observed number of annual earthquakes.
</p>
<p>
  These new findings, and increasingly precise stress maps and their relationship with crustal deformation and earthquake rates, will improve our understanding of earthquake hazard and risk by enabling scientists to better map where future earthquakes are more likely to occur.
</p>

