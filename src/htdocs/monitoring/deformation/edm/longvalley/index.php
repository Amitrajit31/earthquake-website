<p>The two-color EDM network near Mammoth Lakes, California is within
  the Long Valley caldera and has had recent volcanic unrest. The Long Valley
  Caldera is located east of the Sierra Nevada mountains.</p>

<figure class="right" style="max-width: 500px;">
  <img src="../img/lvnet.gif" alt="Map of Long Valley Net" />
  <figcaption>
  This map shows the locations of baselines that are measured in the Long
  Valley Caldera, near Mammoth Lakes, California, using a two-color laser
  distance measuring instrument (geodimeter). The two-color geodimeter
  measures distances to a precision of 0.5 to 1.0 mm for ranges between 3
  and 8 km. The locations of these baselines are shown in relation to
  sources of inflation that have been modeled using both these data and
  leveling data.</figcaption>
</figure>

<p>Plots of the changes in length of 8 frequently measured baselines are
  available through the links listed below. These baselines, which use the
  central station at CASA as a common end point, have been measured
  approximately 3 times each week since early 1984. The general trend of the
  data is: 1) a general decrease in the rate of extension from 1983 through
  most of 1989; 2) an abrupt increase in the rate of extension on several
  baselines to 5 cm/yr from late 1989 through early 1990; and 3) a decrease to
  2 to 3 cm/yr extension rate that has persisted since mid-1990.</p>

