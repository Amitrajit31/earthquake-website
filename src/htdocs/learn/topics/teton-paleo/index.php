<p>
  From September 6 to 21, 2017 USGS scientists did fieldwork on the Teton Fault near Jackson, Wyoming. We followed their work as they prepared the area, dug the trench, gathered data, and then refilled the trench.
</p>
<p>
  The Teton Fault in Wyoming is a 60-70km long feature that follows the base of the Teton Range along the east side. Data from a previous trenching study in the 1990’s showed evidence of large earthquakes during the Holocene (about 11,700 years ago to present). The fieldwork done September 6-21 aimed to answer remaining questions about the timing of the past earthquakes, how many years passed between large earthquakes, the slip rate along the fault, the exact length of past ruptures, and the magnitudes of the earthquakes.
</p>

