    <p>
      The position of the faults will indicate the potential strength and distribution of ground-shaking during an earthquake. This information is used to make the National Seismic Hazard Maps, which are updated with the latest information every few years.  These hazard maps are used by engineers to determine the best building codes for different areas based on how hard and how often the ground shakes. The building codes guide the construction standards for houses, bridges, and other structures to withstand earthquake shaking without collapsing or causing injuries to those inside or nearby.  Ridgecrest is the closest large town to Panamint Valley, but a large earthquake might be felt there as well as in the distant smaller towns surrounding the sparsely-populated valley. Despite its remote location, results from Panamint Valley will help inform hazard estimates in more densely populated regions.
    </p>
