<p>
	Throw a rock into a pond or lake and watch the waves rippling
	out in all directions from the point of impact.
	Just as this impact sets waves in motion on a quiet pond,
	so an earthquake generates  seismic  waves  that
	radiate out through the Earth.
</p>
<p>
	Seismic waves lose much of their energy in traveling
	over great distances. But sensitive detectors (seismometers)
	can record theses waves emitted by even the smallest earthquakes.
	When these detectors are connected to a
	system that produces a permanent recording, they
	are called seismographs.
</p>
<p>
	There are many different types seismometers,
	but they all are based on the fundamental principle - that the
	differential motion between a free mass
	(which tends to remain at rest) and a supporting structure
	anchored in the ground (which moves with the vibrating  Earth)
	can  be  used to record seismic waves.
</p>
<figure class="left" style="max-width:526px">
	<img src="images/keep1a.gif" alt="">
	<figcaption>
		Figure 1. Seismographs are designed so that slight earth
		vibrations move the instruments;
		the suspended mass (M), however, tends to remain at rest,
		and its recording stylus records this difference in motion.
		The horizontal seismograph shown here moves only in the horizontal plane.
		Vertical seismographs, like the simple one shown here,
		use a "soft" link between the earth-anchored instrument and the suspended mass.
		In this design, the mass hangs from a spring, which absorbs some
		of the motion and causes the mass to lag behind actual motion.
	</figcaption>
</figure>
<p>
	This principle is illustrated in figure.
	Vertical support <i>AB</i> holds mass <i>M</i> in position
	by wire <i>AM</i> and by strut <i>BM</i> at point <i>B</i>;
	the system becomes a seismometer when the vertical support
	is embedded in a concrete pier attached to the Earth.
	If there is no friction at the point <i>B</i> and mass <i>M</i>
	is reasonably large, the movement of the pier and the attached upright
	support in response to an earthquake wave will set
	up a differential motion between the mass and the pier
	(the inertia of the mass will make it remain at rest).
	This motion - the signal of an earthquake wave - can then be recorded
	on a revolving drum. When the pier is steady, the pen
	attached to the mass writes a straight line. But when the pier shakes,
	the mass and strut wiggle, recording waves
	from the earthquake that started the boom in motion.
</p>
<p>
	Usually, the drum rotates on a screw-threaded axle so that
	the recording pen moves on a continuously advancing record
	and does not simply repeat the same circle over and over.
	Because time - both the time of day and the synchronization of events -
	is an important element in seismology, clocks are always
	part of a seismograph system.
</p>
<p>
	A single  seismograph  pendulum works  in  only  one  direction,
	and cannot give a complete picture of wave
	motions from other directions.
	To overcome this problem, modern seismograph stations
	have three separate instruments to record horizontal waves -
	(1) one  to record the north-south waves, (2) another
	to record east-west waves, and (3) a vertical one
	in which a weight resting on a spring tends to stand
	still and record vertical ground motions.
	The spring-suspended mass lags behind the motion caused
	by the earthquake, making the pen record the waves on the drum.
	This combination of instruments tells a seismologist
	the general direction of the seismic wave source,
	the magnitude at its source, and the character of the wave motion.
	Instruments at other stations must be used to get a precise fix
	on the earthquake's epicenter.
</p>
<p>
	An earthquake generates a series of waves that
	penetrate the entire Earth and travel at and through its
	surface. Each wave has a characteristic time: each has its
	own move of travel. They are quite complex, but a
	few basic facts will explain how they travel through the
	Earth and how an earthquake's epicenter can be
	determined from seismograph records.
</p>
<p>
	There are four basic types of seismic waves; two
	preliminary body waves that travel through the Earth and
	two that travel only at the surface (L waves).
	Combinations, reflections, and diffractions produce
	an infinity of other types, but body waves are the
	main interest in this discussion.
</p>
<p>
	Body waves are composed of two principal types;
	the P (primary) wave, comparable to sound waves, which
	compresses and dilates the rock as it travels forward
	through the Earth; and the S (secondary) wave, which
	shakes the rock sideways as it advances at barely more
	than half the P-wave speed.
</p>
<figure class="left">
	<img src="images/keep2.gif" alt="">
	<figcaption>
		Figure 2. Travel-time curves with idealized seismograms
		(earthquake records superimposed).
	</figcaption>
</figure>
<p>
	The P wave is designated the primary preliminary
	wave because it is the first to arrive at a seismic
	station after an earthquake. It travels at a speed
	usually less than 6 kilometers per second in the Earth's
	crust and jumps to 13 kilometers per second through the core.
</p>
<p>
	The S wave is the secondary preliminary wave to be
	recorded. It follows paths through the Earth quite
	similar to those of the P-wave paths, except that
	no consistent evidence has yet been found that the
	S wave penetrates the Earth's core.
</p>
<p>
	The lines labeled P, S, and L in the curves shown
	on figure 2 represent the travel time required for each
	phase at distances of 0 to 1300 kilometers from the
	earthquake's epicenter. They mark the points on the record
	at which these waves first arrive at the station.
</p>
<p>
	The simplest method of locating an earthquake on a globe
	is to find the time interval between the P- and S-wave
	arrivals at several seismograph stations. The
	distance to the earthquake from each station is then
	determined from standard travel-time tables and
	travel-time curves. Great-circle arcs are drawn on the
	globe using the distance of the earthquake to the
	station as a radius. All the arcs should intersect at
	a common point - the epicenter.
</p>
<p>
	Another method of locating an earthquake is to use
	the P-wave arrival-time minus origin-time (P - O)
	interval instead of distance. This method is more
	common because the time can be taken directly from
	surface focus travel-time tables assuming an origin of
	00 hours. This method, however, requires that travel-time
	tables be available for various depths of focus.
	For locating a deep shock, one 700 kilometers deep,
	for example, travel-time tables and travel-time curves
	for that depth have to be used to calculate the origin time
	and distances.
</p>
<p>
	Other wave types can be generated inside the Earth
	by P and S waves, as shown in figure 3. As many as
	five different wave groups or phases can emerge when
	a P or S wave encounters a discontinuity or
	interface within the Earth.
</p>

