      The epicenter of an earthquake is the point on a fault where an earthquake starts. Earthquakes happen because of sudden slip on a fault, much like snapping your fingers. Before the snap, you push your fingers together and sideways. Because you are pushing them together, friction keeps them from moving to the side. When you push sideways hard enough to overcome this friction, your fingers move suddenly, releasing energy in the from of sound waves that then travels from your fingers to your ear.
