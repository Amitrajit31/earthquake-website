<h2>Another Large Quake in the next 100 years? Maybe, but..."</h2>

<p>Based on <a href="eqmodels.php"> models</a> taking into account the long-term rate of slip on the San Andreas fault and the amount of offset that occurred on the fault in 1906, the best guess is that 1906-type earthquakes occur at intervals of about 200 years.</p>

<p>Because of the time needed to accumulate slip equal to a 20 ft offset, there is only a small chance (about 2 percent) that such an earthquake could occur in the next 30 years, according to the  <a href="/regional/nca/wg02/">report of the Working Group on California Earthquake Probabilities</a>.</p>

<p>The real threat to the San Francisco Bay region over the next 30 years comes not from a 1906-type earthquake, but from smaller (magnitude about 7) earthquakes occurring on the Hayward fault, the Peninsula segment of the San Andreas fault, or the Rodgers Creek fault.</p>

